### 常见错误

1. **日常建立阶段太短，读者没有建立可解释性基准**：跳过了「技术是可靠的」这个认知基础，后期的「技术崩塌」就失去了对比的力度
2. **排查失败太快，主角放弃理性太容易**：主角在第二次异常后就认定是鬼，损失了赛博灵异独有的理性挣扎过程
3. **证明尝试全部是「没有人相信我」**：主角证明失败的原因应该多样——有时是物证消失，有时是他人看不到，有时是技术专家无法复现，而不是每次都是「他们不相信我」
4. **设备异常没有内在逻辑**：异常的出现方式应该有规律（特定时间/特定触发条件/与某个历史事件关联），而不是完全随机的
5. **结局过于解释性**：把所有谜题都解释清楚，失去了赛博灵异的余韵——最好的结局是「解释了一些，留下了更大的问题」',
