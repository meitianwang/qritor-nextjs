### 第一阶段：日常建立（关键铺垫）

**目标**：让读者和主角共同建立「这是一个高度可解释的智能家居环境」的认知基准线。

**需要展示的内容**：
- 每个设备的正常运作方式（用几个「日常使用场景」自然带出）
- 主角对技术的熟悉程度（「我是程序员，这些设备的工作原理我很清楚」vs「我不懂技术，只是用它」——两种主角类型制造不同的恐惧质感）
- 邻居环境和物业关系（后期需要求助时的情感成本铺垫）

**铺垫要求**：
- 至少展示 2-3 个设备的「正常误报」（音箱误触发、摄像头被树枝遮挡……）——建立「设备会出错」的认知，让后期的真正异常被主角初期误判为「又是误报」
- 展示主角在这个小区居住的时间长度和心理归属感——归属感越强，「家不再安全」的恐惧越有力

