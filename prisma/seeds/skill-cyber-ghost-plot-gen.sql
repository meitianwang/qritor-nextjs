### 短篇与中长篇的结构差异

**短篇（3000-10000字）**：
- 聚焦单一设备的异常弧（一台摄像头，一个扫地机器人）
- 排查失败阶段简化，直接进入证据困境
- 结局可以是开放的：主角面对最后一个无法解释的证据，故事在那里停止
- 字数不够时：牺牲「日常建立」，用主角的内心独白快速建立可解释性基准

**中篇（10000-30000字）**：
- 2-3 台设备，每台设备有独立的异常弧，但三条弧在中后期交汇
- 完整的排查失败 → 证据困境流程
- 至少一次「以为找到了技术解释，但解释被推翻」的假解决
- 结局需要是「完整的」，不能只是开放式收尾

**长篇（30000字以上）**：
- 设备异常可以有「版本迭代」：第一阶段的异常消失了，第二阶段出现了更难解释的新异常
- 可以引入其他住户的视角——他们也有类似的经历吗？还是这是只针对主角的？
- 「物业监控室」可以作为中后期的关键场景，主角进入监控室看到的内容重构整个故事

