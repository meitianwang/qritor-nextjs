### 第二阶段：第一异常（钩子设计）

**关键原则**：第一个异常必须「足够奇怪，但又足够可以解释」。

**好的第一异常**：
- 监控摄像头的一段录像，录像里主角背后有一个模糊的人形，但主角知道当时家里只有自己——「画质不好，可能是压缩失真」
- 扫地机器人在固定的一个角落停止，每次到那里就原地旋转一圈然后绕开——「激光雷达读到了障碍物，可能是光线折射」
- 音箱在夜里自动唤醒播放了一首歌，那首歌是主角已故父亲最喜欢的——「误触发，歌单里有这首歌」

**叙事功能**：主角的第一反应是找技术解释，而且能找到一个「说得通」的解释——读者和主角一起完成了这个理性解释，同时读者已经感觉到「这个解释不够充分」。

