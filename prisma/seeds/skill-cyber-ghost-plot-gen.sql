### 第四阶段：证据困境（赛博灵异核心矛盾）

**证明悖论**：

赛博灵异最核心的叙事矛盾：「我有设备，所以我应该能够证明。但设备记录的东西无法被证明，甚至证明的过程制造了新的无法解释的情况。」

**三种证明尝试与失败方式**：

**给他人看录像**：
主角找到了一段异常录像，想让朋友/家人/物业确认。但当他们看的时候，录像里「什么都没有」——或者他们看到的和主角看到的不一样。设备的记录内容因观看者的不同而不同，这打破了「客观证据」的基础假设。

**调取第三方数据**：
主角联系物业调取楼道摄像头的数据，想用第三方证据交叉验证。但物业摄像头的存储周期刚好覆盖了关键时段，或者那段时间楼道摄像头「恰好」发生了故障。第三方证据系统性缺失。

**技术专家鉴定**：
主角请来程序员朋友/安全工程师检查设备。专家检查后无法复现异常，给出「一切正常」的结论。但在专家离开后的当天晚上，异常以更强烈的形式再次出现——「它知道有人在检查它」。

**叙事功能**：每一次证明尝试失败，不只是「没有证明成功」，而是让主角的处境更加孤立——他越来越无法让任何人相信他，而设备继续以只有他能感知到的方式运作。

