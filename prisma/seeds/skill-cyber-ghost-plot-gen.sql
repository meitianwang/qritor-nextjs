### 第三阶段：排查失败（核心张力来源）

这一阶段是赛博灵异故事独有的叙事引擎：**技术排查过程本身成为恐惧的生产者**。

**排查失败的三种模式**：

**技术解释穷尽型**：
主角逐一排查所有技术可能性（重启设备/更新固件/检查网络/联系技术支持），每一个可能性都被排除了，但异常还在继续。「我已经做了所有应该做的事，但什么都没有改变。」

**解释相互矛盾型**：
找到了两种不同的技术解释，但这两种解释不能同时成立——「要么是 A 造成的，要么是 B 造成的，但 A 和 B 不可能同时发生」。主角无法确定哪种解释是对的，或者两种解释都不是真正的原因。

**解释本身产生新问题型**（最强模式）：
主角找到了一个技术解释并解决了，但解决方案触发了新的异常——「我把摄像头重置了，消除了那段奇怪的录像，但重置之后，每天晚上 3:17 摄像头会自动重新定向到同一个角落。」修复导致了比原来更难解释的现象。

**排查日志的叙事价值**：
主角记录排查过程的笔记/聊天记录/客服对话，这些「技术排查证据」在后期会成为重要的叙事道具——它们证明了主角穷尽了理性解释，也为转向「非技术解释」提供了心理许可。

