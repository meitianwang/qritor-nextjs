### 字段联动关系

- **叙事视角 → 主角设定.侦查风格**：第一人称限知最适合"逻辑推演型"主角（读者跟着主角思考）；心理博弈型主角有时需要第三人称才能展示他人的反应
- **推理节奏 → 阴谋框架.真相层次**：快节奏适合2层反转；慢节奏能支撑3层反转
- **悬疑氛围 → 故事钩子.读者预期**：氛围设定决定了读者实际上能获得什么体验，要与读者预期一致

