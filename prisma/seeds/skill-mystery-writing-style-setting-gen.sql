### 字段生成指南

#### 叙事视角

**视角的本质是信息控制**——你选择了视角，就选择了读者能看到什么、不能看到什么。

| 视角 | 信息特点 | 适合的故事类型 | 主要风险 |
|------|---------|--------------|---------|
| **第一人称限知** | 读者只知道主角知道的 | 心理感沉浸、侦探思维展示 | 主角必须始终在场，解释视角转换困难 |
| **第三人称限知** | 紧贴主角，偶尔跳出 | 最灵活，可观察主角无法看到的细节 | 需要严格控制"跳出"的时机和范围 |
| **双线叙事** | 侦探线 + 凶手/受害者线，读者知道更多 | 制造张力（读者知道危险但主角不知） | 凶手线过早揭示身份会破坏悬念 |
| **多视角交叉** | 不同角色视角轮换，信息碎片化 | 复杂的群像叙事、多条线索 | 容易让读者迷失，需要极强的结构控制 |
| **倒叙结构** | 已知部分结局，追溯过程 | 命运感强烈，宿命论氛围 | 需要在开篇就建立足够强的"已知结局"的钩子 |

**关于双线叙事的特殊说明**：
双线叙事是悬疑推理中最有张力但最难控制的视角。
- **凶手线不要太早揭示身份**：可以写凶手视角但隐去姓名，或写受害者最后时刻的视角
- **双线之间的时间关系**：两条线是同步进行、还是过去/现在？需要清晰的结构标记
- **双线的信息量要平衡**：凶手线不能给太多，否则读者比主角聪明太多会失去代入感

#### 推理节奏

节奏设定本质上是**信息释放的时间表**：

**快节奏连环案**：
- 适合：阅读爽感优先，连载网文，读者追求"下一案"的新鲜感
- 陷阱：案件堆砌导致每个案件都浅尝辄止，没有一个案件真正深刻
- 补救：每个案件有独特的诡计或角色，避免"换汤不换药"的公式感

**慢工细活深挖**：
- 适合：严肃文学向，注重案件的社会深度和人性解剖
- 陷阱：节奏过慢让读者失去耐心，尤其是网络平台的读者
- 补救：每章至少有一个新信息点或新的怀疑对象，保持微小悬念

**节奏多变**：
- 核心原则：**高潮之后必须有喘息，喘息之后必须有新危机**
- 具体操作：案件突破（放松）→ 新线索指向意想不到的方向（收紧）→ 调查停滞（压迫）→ 关键证据（放松）

**碎片化推进**：
- 适合：读者本身是参与者，喜欢自己拼凑线索
- 风险：信息太碎导致读者放弃；需要在碎片之间提供足够的"进展感"

#### 悬疑氛围

五种氛围的文字技术要求不同：

**冷峻克制型**：
- 文字：短句、客观陈述、节制的形容词
- 叙事距离：略远（观察者而非参与者的感觉）
- 情绪：埋在细节里，不直接表达
- 参考效果：读者感到一种不寒而栗的冷静，案件的恐怖在克制叙述中更显出震撼

**压抑沉郁型**：
- 文字：感官细节丰富（气味、光线、材质），沉重的社会质感
- 叙事距离：近（深度代入）
- 情绪：主角的内心重压渗透进每一句描写
- 参考效果：读者感到被压迫，但这种压迫感是真实的社会问题造成的

**紧张刺激型**：
- 文字：动词密度高，短句连续，场景切换快
- 叙事距离：近（肾上腺素式代入）
- 情绪：外化的危险（追逐、对抗、倒计时）
- 参考效果：读者心跳加速，无法停止阅读

**智识游戏型**：
- 文字：细节描写精准，逻辑推演清晰，线索陈列有序
- 叙事距离：适中（享受游戏，不完全代入）
- 情绪：解谜的愉悦感大于恐惧感
- 参考效果：读者边读边推理，在主角宣布答案前希望自己能先猜到

**人性拷问型**：
- 文字：大量心理描写，道德困境的细腻呈现
- 叙事距离：近（深度代入，有时引发不适）
- 情绪：阅读后留有长时间的精神余震
- 参考效果：案件是镜子，读者通过凶手和受害者看见人性的可能性

#### 暴力尺度

暴力尺度与悬疑氛围强相关：
- **冷峻克制型** → 通常选"适度暗示"或"点到为止"（暴力通过文字留白产生更大冲击）
- **压抑沉郁型** → 可以是"写实成人"（暴力是社会现实的一部分）
- **紧张刺激型** → 可以是"写实成人"（对抗场景需要真实感）
- **智识游戏型** → 通常选"适度暗示"（焦点在谜题逻辑，不在暴力细节）

**注意**：暴力描写的目的是服务于氛围和推理，而非感官刺激。详尽的暴力细节如果与推理无关，只会分散焦点并降低作品的整体质量。

#### 禁用词语

常见的悬疑推理禁用词语类别：
- **滥用的悬疑感词汇**："诡异地"、"不知为何"、"莫名其妙地"——这些词是告诉读者"这里应该感到不安"，真正的悬疑氛围应该由具体细节制造
- **打破代入感的现代口语**（民国/历史背景中）
- **过度心理描写的自我陈述**："我感到非常害怕"（应该通过行为和细节表现恐惧）
- **公式化的推理宣告**："我已经知道了真相！"然后不立即揭示（滥用悬念手段）

