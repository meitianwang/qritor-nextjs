### 常见错误

1. **视角不一致**：整体是第三人称，但某章突然变成第一人称——破坏沉浸感
2. **节奏失控**：前半部分快节奏（连环案），后半部分突然慢下来（处理大阴谋），节奏感断裂
3. **氛围与内容错配**：写"冷峻克制"的故事，但句子里充斥着情绪性形容词
