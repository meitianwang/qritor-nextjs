### 科技术语的使用原则

**核心原则**：科技术语不是装饰，是恐惧的锚点。

**正确的使用方式**：
- 用具体的技术细节来强化「这不应该发生」的不可能性：「H.264 压缩伪影的特征是块状噪点，不是透明的轮廓；我确认过了，这个画面里的东西不是压缩伪影」
- 用技术术语作为「主角用尽了理性手段」的证明：「我查了固件版本、重新训练了路径算法、更换了传感器——三周后，它还是绕开那个区域」

**错误的使用方式**：
- 铺陈技术细节但与恐惧叙事无关（读者会感到被科普）
- 用技术术语来填充空间，而不是推进恐惧
- 过于专业到普通读者无法理解（失去代入感）

**「外行视角」的特殊价值**：
对于非技术背景主角，「我不懂这个设备」本身就是恐惧来源——设备变成了一个黑箱，主角不知道「正常」和「异常」的边界在哪里，这种无知比精确的知识更令人不安。

