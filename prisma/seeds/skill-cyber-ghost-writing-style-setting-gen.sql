### 节奏控制

**赛博灵异的特殊节奏问题**：主角需要花时间「查录像/分析数据/排查故障」——这些操作过程如何写而不变成流水账？

**解决方案**：
- 「调查过程」中穿插主角心理状态的变化（越查越不对，越查越想停下来）
- 「调查结果」和「调查过程」可以分开——结果先告诉读者，过程简述
- 最重要的不是「查到了什么」，而是「查的过程中主角内心发生了什么」

