### 理性化崩塌的叙事节奏

赛博灵异的叙事弧是**理性主义者的去理性化过程**，这是这个类型的主要戏剧张力：

```
阶段一：分析者（「我能解释这件事」）
↓
阶段二：怀疑者（「我给出了解释，但我没有说服自己」）
↓
阶段三：焦虑者（「我开始希望这只是故障，而不是试图找出真相」）
↓
阶段四：观察者（「我已经停止解释，只是在记录」）
↓
阶段五：行动者（「我必须做什么」）
```

**每个阶段的叙事语言特征**：

- **阶段一**：「可能是……」「应该是……」「按照产品规格这属于正常范围」
- **阶段二**：「理论上是……但……」「我告诉自己是……但不知道为什么」
- **阶段三**：「我把录像删了」「我重置了设备」（回避性行为，而非解释性语言）
- **阶段四**：只描述观察，不提供解释；句子变短；事实陈述代替了猜测
- **阶段五**：行动性语言，但行动方向是错误的或代价高昂的

