### 证据可信度设定的叙事处理

#### 不可靠叙事
- 叙事技巧：偶尔出现「我当时是不是只是太累了」「事后想想，那段录像我只看了一秒，可能是我记错了」
- 效果：读者需要同时判断「主角是否可信」和「这件事是否真实」，双层悬疑
- 使用限制：不可靠叙事不能过度，否则读者停止投入

#### 可靠但无法证明
- 叙事技巧：主角描述的异常非常具体（时间、设备型号、具体内容），但没有其他目击者
- 效果：读者相信主角，但理解为什么没有人相信他——「理性的孤独」
- 使用要点：主角的叙事可信度越高，他人的不相信就越令人沮丧

