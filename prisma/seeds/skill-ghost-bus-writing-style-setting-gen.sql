### 常见错误

1. **视角跳跃**：第一人称故事中偶尔出现主角不可能知道的信息（「就在他不注意的时候，第三排的东西转过了头」），破坏视角一致性
2. **恐惧密度过高**：开篇就充满「不对劲」，读者的恐惧感知饱和，后续真正的高潮反而显得平淡
3. **规则解释占用叙事空间过多**：用大段内心独白分析规则，打断了叙事节奏。规则的理解应该通过行动和反应展示，而不是说出来
4. **忘记主角有情绪**：规则流很容易变成「主角在解谜」而忘记他也在极度恐惧。恐惧、疲惫、绝望、片刻的希望——这些情绪变化使主角成为真实的人，而不是规则游戏的玩家
