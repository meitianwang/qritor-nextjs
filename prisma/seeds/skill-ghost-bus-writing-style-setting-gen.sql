### 叙事视角的选择

#### 第一人称（推荐）
- 优势：主角的恐惧直接传递给读者，内心活动无需中介，「我看到」比「他看到」有更强的临场感
- 劣势：只能呈现主角感知到的信息，视角外的内容必须通过「主角没看到的声音」「主角后来发现的结果」来表达
- 规则流特别优势：规则流最重要的叙事元素是「主角注意到了什么」——第一人称的选择性注意天然制造悬疑（「我的目光一直避开第三排」比「主角避开了第三排」更令人不安）

#### 第三人称限知
- 优势：比第一人称稍微多一点叙事距离，适合需要偶尔展示「主角没有意识到的危险」的故事
- 劣势：情感共鸣略弱，需要更多笔墨来建立内心活动
- 适用：希望读者有时比主角「知道更多」，制造信息落差的故事

#### 第二人称（实验性）
- 优势：「你」字强制代入，读者成为主角，体验最直接
- 劣势：中文读者对第二人称叙事的接受度参差不齐，且很难维持超过几千字
- 适用：极短篇、开头的氛围建立段落，不建议全篇使用

