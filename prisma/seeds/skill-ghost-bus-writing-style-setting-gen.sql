### 恐惧升级模式

#### 渐进式（最常见）
- 结构：每经过一站，危险程度线性上升，乘客越来越不像人
- 优点：节奏稳定，读者可以预期升级的存在
- 缺点：太可预期，读者会在心理上「提前适应」即将到来的恐怖
- 关键技巧：在渐进的整体趋势中加入「突然的平静」（第三站非常安静——这种平静比预期的恐惧更令人不安）

#### 脉冲式
- 结构：平静—突发恐惧—短暂平静—更强的突发，循环往复
- 优点：读者无法预判下一次冲击何时到来，持续保持警觉
- 缺点：需要精确控制节奏，每次「平静」期不能太长（会让读者松懈）也不能太短（会让读者麻木）
- 关键技巧：两次冲击之间的平静期，用细节暗示「平静是假象」（「他们都安静地坐着，像蜡人，除了那个偶尔低头看手机的女人——但我不记得她从哪一站上来的」）

#### 倒数式
- 结构：明确告知还有几站，每一站都是一次倒计时
- 优点：时间压力直接可见，每到一站读者都会感到进度条推进
- 缺点：过于公式化，每站的恐惧被结构化；如果每站体验质量参差，节奏会崩
- 关键技巧：倒数式必须配合「每一站都有独特的内容」，不能让读者感到只是在数数

#### 规则崩塌式
- 结构：规则本身开始失效或变化，不确定性是恐惧来源
- 优点：最深层的恐惧——连「遵守规则就能安全」这个基础保障也在崩溃
- 缺点：需要在前期充分建立读者对规则的信任，才能在后期打破它
- 关键技巧：规则崩塌不应该是「规则突然失效」，而应该是「主角发现之前理解的规则是错的」

