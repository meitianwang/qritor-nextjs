### 规则流文风的特殊要求

#### 「正常基线」的建立
末班车故事的恐惧来自「偏离正常」，因此需要先建立「正常感」——即使在超自然情境下，主角应该先在心里建立「如果这是一辆普通的车，应该是什么样的」基准。之后每一个偏差才有意义。

**开篇建议**：用一小段主角对「普通的深夜公交」的认知（哪怕是回忆或想象）作为隐性的对比基准。

#### 局限空间的叙事技巧
全程发生在一辆车上，空间极度受限。防止读者感到单调的方法：
- 利用纵向空间（车厢前排/中部/后排，每个区域有不同的危险等级）
- 利用视线盲区（驾驶室与乘客区的隔断、看不到的窗外角落）
- 利用时间节点（每一站是一次空间状态的重置——新的乘客，变化的车厢）

#### 克制与渲染的平衡
- **克制的写法**：「第四排多了个人，我不记得他从哪里上来的。」——留白，让读者补全恐惧。
- **渲染的写法**：「他的脖子比正常人长，皮肤像陈旧的蜡，眼睛盯着我的方向，但我不知道他能不能看见。」——给出细节，制造具体的视觉恐惧。
- **原则**：开篇用克制，中段逐步增加渲染，高潮时允许完整的感官描写，结尾再回到克制。

