### 信息管理策略

规则流的核心叙事张力来自**信息的落差与揭示**：

**主角知道的 vs. 读者知道的**：
- 理想状态：读者在某些时候比主角「先一步」发现危险（看到了主角没注意到的细节），但在更多时候与主角同步——信息落差是制造「我想喊出来但主角听不到」的恐惧的关键

**规则已知的 vs. 规则未知的**：
- 每一个「新规则被发现」的时刻都是一个高潮点，这个时刻不应该被草草带过
- 节奏建议：发现新规则→对新规则的理解/误解→应用/违反新规则→发现对新规则的理解有误——形成一个小闭环

**感官描写的优先级**（从高到低）：
1. 视觉：规则流最依赖视觉信息（「那个东西在看我」「司机的脖子角度不对」）
2. 听觉：声音是规则流第二重要的感官（「车轮的声音不太对」「后排有什么在均匀地呼吸」）
3. 温度：温度变化是恐惧的隐性信号（「我的手开始发冷，不是因为车内空调」）
4. 气味：被大多数规则流作者忽视，反而是差异化的机会

