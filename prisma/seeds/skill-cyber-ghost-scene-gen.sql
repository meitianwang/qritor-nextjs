### 常见错误

1. **所有异常集中在一个房间**：只有客厅/只有卧室出现异常，其他空间从未被涉及，失去了空间危险扩散的叙事张力
2. **没有设计监控盲区**：全覆盖的监控让故事失去了悬念，总能看到发生了什么
3. **公共区域没有被利用**：电梯录像、门禁记录这些独立证据来源没有出现在故事中
4. **区域历史与当前异常脱节**：两件事看起来毫无关联，主角即使查到了历史也无法整合
5. **危险等级一成不变**：从头到尾某些区域「安全」，某些区域「危险」，失去了恐惧扩散的紧迫感',
