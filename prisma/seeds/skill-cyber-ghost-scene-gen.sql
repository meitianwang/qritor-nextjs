**楼道/走廊**：
- 设备密度：中等（楼道灯/偶尔的摄像头）
- 恐惧质感：过渡空间的异常，「在家门外」的威胁感
- 关键叙事功能：主角的门铃摄像头看到了走廊，他人的行为可以被记录
- 设计要点：走廊的灯光模式（人体传感器触发）是天然的异常检测器——灯亮了，说明什么东西经过了

