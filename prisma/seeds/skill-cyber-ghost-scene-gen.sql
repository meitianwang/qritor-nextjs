**客厅**：
- 设备密度：通常最高（智能电视/音箱/摄像头/灯光）
- 恐惧质感：日常生活空间的异常，打破了「家是安全的」的基础假设
- 关键叙事功能：异常最密集的地方，也是主角最先发现问题的地方
- 特殊叙事价值：当主角开始「坐在客厅盯着设备」，客厅本身成了紧张关系的舞台

**浴室**：
- 设备密度：最低（隐私考量）
