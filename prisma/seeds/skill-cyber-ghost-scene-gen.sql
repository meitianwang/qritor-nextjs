### 区域的历史事件设计

每个关键区域应该有一段历史，这段历史与当前的异常有关——但不应该在故事一开始就完整揭示：

**历史事件的「可发现性」**：
- 主角可以通过调查（查物业记录/问邻居/搜索新闻）逐步拼凑出历史
- 历史不应该「一目了然」，而应该是碎片化的，需要主角主动整合

**历史与设备的联系**：
- 最有力的设计：历史事件与现在的设备异常有精确的时间对应（「就是在那件事发生后，我的扫地机器人开始绕开那个角落」）

---

### 危险等级的动态性

**区域危险等级不是固定的**——随着故事进展，某些区域会升级或降级：

| 故事阶段 | 卧室 | 客厅 | 楼道 | 物业监控室 |
|---------|------|------|------|----------|
| 开始 | 安全 | 轻度 | 安全 | 未知 |
| 中期 | 轻度 | 高度 | 轻度 | 核心 |
| 后期 | 高度 | 核心 | 高度 | 核心 |

**区域升级的叙事意义**：每次一个「安全区」变成了危险区，主角可退守的空间就缩小了一步——这是空间性的倒计时。

