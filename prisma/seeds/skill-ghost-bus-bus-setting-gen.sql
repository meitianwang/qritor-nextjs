### 字段生成指南

#### 车型

不同车型的叙事优势：

| 车型 | 幽闭感 | 社会代入感 | 逃脱难度 | 适合的故事长度 |
|------|--------|----------|---------|-------------|
| **城市公共汽车** | 中等 | 最高（所有人乘过） | 中等 | 短中长篇均可 |
| **长途客运大巴** | 较高（路途更长、更偏僻） | 中等 | 高（路途中无法下车） | 中长篇 |
| **地铁/轻轨** | 高（地下、密闭） | 高（通勤经历） | 高（地下无出路） | 中篇 |
| **有轨电车** | 较低（玻璃多） | 特定城市感 | 中等 | 氛围向短篇 |

**车型与规则的关联**：车型决定了哪些规则有物理意义。地铁规则可以涉及车门开关；公共汽车规则更多涉及座位和司机；长途大巴可以有更丰富的车厢空间规则。

#### 路线编号

路线编号设计的叙事维度：
- **真实存在的路线**（如"57路"）：读者可能乘过，代入感最强，但主角需要解释为什么没察觉出不对
- **刚好差一个数字的路线**（如普通路线是"57路"，这辆是"570路"）：接近真实但不是真实，主角最初可能误认
- **完全不存在的路线编号**：如"X57路""灵车"等，立刻建立了异质感，但代入感略弱
- **没有路线编号**（空白牌或显示"回场"）：最诡异，也最真实（深夜确实有空载车辆）

#### 外观特征

末班车的外观应遵循"**接近真实，但有无法忽视的细节**"原则：

**推荐的外观异常类型（由轻到重）**：

```
【克制级——初始印象时使用】
- 车身涂装颜色比同路线的车深了几个色度
- 车牌略旧，字体是已经停用的旧式字体
- 侧面的"紧急出口"标签撕掉了，但黏胶痕迹还在
- 路线标示牌玻璃有一道裂纹，刚好穿过数字

【中等级——第二次仔细打量时发现】
- 车身没有任何广告，现实中的公共汽车几乎不可能完全没有广告
- 车窗玻璃的反光角度不对——窗外的路灯应该在左边，但反光在右边
- 前灯刺得人眼睛酸，但没有光源炫目的感觉——更像是光在消失
```

**避免**：直接的超自然外观（"车身在发光""车体内有人影"）作为初始外观描写。这些可以出现在夜深后。

#### 车内氛围

车内氛围是建立持续压迫感的关键，需要调动多个感官：

**灯光**（规则流最重要的视觉元素）：
- 荧光灯管的闪烁频率比正常的慢——就慢一点，但足以让人头疼
- 颜色偏冷、偏蓝，使所有乘客的肤色看起来不健康
- 某一节车厢的灯亮度不同，像是不同时代的灯
- 灯光从不完全熄灭，也从不完全正常

**气味**（常被忽视但极有效）：
- 旧布和机油的混合气味——比现实中的公共汽车更重
- 一种主角说不清的甜味，不是香水，更像是……腐败之前的糖分
- 绝对没有食物的气味（现实公共汽车有），这种"缺失"本身就是异常

**空气质感**：
- 比外面更凉，凉得不合理（冬天还好，夏天深夜一辆凉意渗骨的公共汽车）
- 有一种轻微的压迫感，像是气压略低
- 声音比外面静——外面的城市噪音进不来，但车内噪音也比正常的少

#### 司机特征

司机是规则流中最特殊的存在：**他不是敌人，他是规则的一部分**。

**司机设计的三个原则**：
1. **不主动伤害**：司机是运行者而非猎手。他只是在开车，遵守他自己的规则
2. **不可沟通**：可以开口，但说的话没有意义，或者只在规则范围内回应
3. **存在本身令人不安**：不是明显的恐怖，而是某种根本性的"不对"

**司机的身体异常类型**：
- 脖子转动角度超出人类正常范围，但动作缓慢自然
- 从后视镜能看到他的眼睛，但无论主角坐在哪里，他的眼睛都在对准主角
- 他的呼吸声在驾驶室，但气雾凝结在主角头顶
- 他从不眨眼——这一点需要很近才能察觉

**司机与规则的关系**：司机通常是"规则的执行者"而非制定者。他在确保乘客遵守规则（否则会有后果），但他本身不受某些规则约束。

#### 运行规律

运行规律的设计决定了故事的时间框架：
- **出现条件**：是什么让这辆车出现？每天深夜某时？只在特定气候/节气？只有满足某条件的人才能看到它？
- **路线合理性**：这辆车的路线是否大体上和真实路线重叠（然后逐渐偏离）？还是从一开始就走了不可能的路？
- **时间异常**：车上的时间与现实时间是否同步？（这直接影响"有没有人在找主角"的外部压力）

