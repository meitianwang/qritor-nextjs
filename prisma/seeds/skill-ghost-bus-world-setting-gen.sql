### 终点站性质

终点站的设定决定了故事的终极赌注——主角如果失败，会发生什么：

| 终点类型 | 叙事含义 | 适合的恐惧质感 |
|---------|---------|------------|
| **冥界/黄泉** | 传统东方死后世界，有明确的规则体系 | 文化共鸣感强，可与民俗联动 |
| **虚空/遗忘之地** | 没有死后世界，只有消失——比死亡更恐惧 | 存在主义恐惧，现代感强 |
| **过去的某一刻** | 被困在某个历史时间点，回不来 | 孤独感，时间错位的绝望 |
| **平行时空** | 外表与现实相似但主角是异物 | 「几乎正常」的深层不安 |
| **某个实体的居所** | 终点是某个古老存在的地盘 | 具体的危险，可以有面孔 |
| **完全留白** | 到过终点的人没有回来，无人知道那里是什么 | 最强的未知恐惧 |

**留白的力量**：「完全留白」处理看似偷懒，实则需要技巧——必须通过间接证据暗示终点的可怖，而不是直接描述。那些没有回来的人留下了什么痕迹？

