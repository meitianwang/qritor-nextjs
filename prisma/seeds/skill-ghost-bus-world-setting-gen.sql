### 乘客来源——非人存在的类型

不同类型的非人乘客带来的恐惧质感完全不同：

#### 执念未散的亡魂
- 行为特征：重复某个生前的习惯，不知道自己已死
- 恐惧质感：悲凉、孤独，「我们都会变成这样」的存在主义恐惧
- 叙事功能：他们不是主动威胁，存在本身令人不安
- 设计重点：他们的重复行为应该**有具体内容**（一直看窗外/一直翻手提包/一直低声说某句话），而不仅仅是「坐在那里」

#### 阴间的差役/使者
- 行为特征：明白这辆车的意义，以「行政」态度处理一切
- 恐惧质感：冷漠的官僚式态度，他们在乎规则不在乎主角
- 叙事功能：与主角的冲突来自「你不该在这里，但你在了，所以…」
- 设计重点：他们的语言和行为应有某种**程序性**，不带感情色彩的程序性比愤怒更令人不安

#### 以人形出现的捕猎者
- 行为特征：伪装成普通乘客，等待主角犯错
- 恐惧质感：不信任任何人，不确定谁是真正的危险
- 叙事功能：制造人际层面的恐惧，与规则体系的「不要引起注意」直接联动
- 设计重点：伪装越完美越好。外表完全正常，破绽只在细节（呼吸节奏不对、眨眼频率异常、对话中说漏了不该知道的事）

#### 规则的具现化
- 行为特征：某些规则以存在的形式体现，「违反规则三，第四排的东西会出来」
- 恐惧质感：规则与惩罚之间的关系变得可见，让规则体系有了「面孔」
- 叙事功能：违规不再是抽象的后果，而是具体的实体出现
- 设计重点：这类存在**不应有人格**，更接近自然法则的具现化，而非有意志的敌人

