### 阴阳界规则——现实世界如何响应

在这趟车行驶过程中，现实世界的运作方式：

- **时间流逝**：现实时间是否与车上同步？（如果现实时间正常流逝，是否有人在找主角？）
- **通讯状态**：手机是否有信号？（有信号但主角无法说清位置 vs. 完全断联，制造的恐惧质感不同）
- **外部干预**：有没有人能从外部帮助主角？（完全孤立 vs. 可以获得有限帮助）
- **身份状态**：车上时主角的身份是否在人间「存在」？（是否有人能感受到他的消失）

**设计建议**：完全断联比有信号但无法描述位置更容易写，但后者制造的无力感更令人窒息。

