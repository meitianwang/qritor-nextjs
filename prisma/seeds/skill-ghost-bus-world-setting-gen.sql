### 处理禁忌：不要过度解释

以下信息**通常不需要**在世界规则模块中明确设定：

- 车票如何购买（让它成谜比解释更好）
- 司机的完整身份和历史（保留神秘感）
- 每条规则的起源（只需要规则，不需要立法史）
- 其他乘客各自的完整故事（他们的故事是点缀，不是主轴）

