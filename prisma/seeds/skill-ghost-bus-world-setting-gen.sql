### 主角卷入原因

普通人为什么能上这辆车——答案直接影响故事的代入感和主角的先验优势：

**特殊体质**（阴阳眼/鬼胎/曾死回生）：
- 优点：解释清晰，主角有一定先验知识，可以更快理解规则
- 缺点：设定负担，需要解释体质来源；主角的特殊性减弱了「任何人都可能遇到」的恐惧
- 适合：希望主角有一定主动权的故事

**特定情绪状态**（极度绝望/求死之心/亲历重大创伤）：
- 优点：情感代入度高，解释了主角为何独自在深夜
- 缺点：需要在开头建立主角的情绪底色
- 适合：情感向故事，把「求生意志的重建」作为内核

**完全随机**（就是运气不好）：
- 优点：「任何人都可能遇到」的恐惧最强，现代都市传说感最足
- 缺点：主角没有任何先验优势，全靠机智和运气
- 适合：纯粹的生存恐惧短篇

**被主动选中**（某个存在「选了」这个主角）：
- 优点：加入「为什么是我」的谜题层，赋予故事更深的意义
- 缺点：需要在结尾合理解释「被选中的原因」，否则悬而未决
- 适合：有更大叙事野心的中长篇

