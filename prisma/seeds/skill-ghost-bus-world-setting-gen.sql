### 末班车存在原因——解释深度的选择

**三个深度层次**：

```
【最浅层——只有结果，没有原因】
示例格式："深夜某时，这辆车就会出现在某个落单的人面前"
读者和主角一样不知道原因，只知道它存在。
适合：强氛围短篇，「不可知」本身就是恐怖来源

【中层——有历史背景，没有完整解释】
示例格式："五十年前这条路发生过重大事故，从那以后每逢特定日期…"
给出历史依据，但不提供形而上学解释。
读者有「感觉上合理」的依据，同时谜底保持完整。
适合：中篇，平衡神秘感与世界观厚度

【深层——有完整的两界规则体系】
示例格式："阴阳两界之间需要通道，这辆车是某个古老协议的产物，已运行数百年…"
世界观完整，与其他传说可以联动。
适合：长篇，世界观是故事的重要部分
```

**警告**：选择深层解释时，设定出来的两界规则体系必须在故事全程保持自洽。任何与设定矛盾的情节都会破坏故事的可信度。

