### 四、悬疑氛围的营造

#### 恐惧感的来源（排序从高到低）

1. **认知失调**：某件事本不应该发生，但发生了，而且没有解释
2. **信任崩塌**：曾经以为可以相信的人可能在撒谎
3. **威胁不可见**：知道有危险但不知道危险在哪里
4. **时间压力**：必须在某个截止点前找到答案
5. **感官细节**：气味、声音、光线的异常

#### 张力积累技巧

**信息落差制造法**：
读者知道了某件事，但主角还不知道（尤其是双线叙事中）。这种信息落差会制造持续的阅读紧张感（"他不知道那个人就在背后！"）

**暗线提示法**：
每隔若干章节，埋下一个细小的"不对劲"——不解释，不强调，只是让它在那里。随着这些细节积累，读者开始感到有什么事情正在发生。

**节奏收紧法**：
临近高潮时，句子变短，段落变短，章节变短。节奏本身就是氛围的一部分。

