### 五、节奏把控

**章节结尾的钩子设计**：
悬疑推理的每章结尾有三种主要钩子类型：
- **问题钩子**：新信息提出了一个无法回避的问题
- **危机钩子**：主角或重要人物面临新的危险
- **反转钩子**：之前以为是真的东西被证明不对

**节奏疲劳的预防**：
长篇中，如果所有章节都是高张力，读者会麻木。
- 每5-7章安排一个相对"低强度"的章节（线索梳理、人物关系发展、城市漫游式场景）
- 这种间歇不是浪费，而是为下一个高潮积蓄情感能量

