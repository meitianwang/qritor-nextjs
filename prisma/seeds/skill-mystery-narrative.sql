### 三、审讯对话的写法

审讯是悬疑推理小说中最考验对话能力的场景。核心原则：**每个审讯对话都是一场双方信息博弈**。

**四种审讯节奏**：

**正面逼问型**：
- 主角掌握证据，逐步出示，压缩嫌疑人的谎言空间
- 写法重点：嫌疑人的回应如何随着证据出示而变化（从从容→微小破绽→防御性反应）

**侧面试探型**：
- 主角没有直接证据，通过旁敲侧击引导嫌疑人自我暴露
- 写法重点：对话表面看起来随意，实则每一个问题都是试探，读者要能感受到这种意图

**信任建立型**（对证人）：
- 目的是打破对方的防御，让他说出不愿说的信息
- 写法重点：主角放低姿态，表现出共情，真正倾听（但内心在分析）

**心理对抗型**（对主要嫌疑人/真凶）：
- 双方都知道对方在做什么，是明牌博弈
- 写法重点：语言的双重含义（表面在说X，实际在传递Y），停顿和沉默的分量

**审讯对话的常见陷阱**：
- ❌ 嫌疑人太快崩溃：对话节奏太短，一两轮就全部交代，失去张力
- ❌ 信息量过大：一次审讯获得太多关键信息，整个推理没有延续性
- ❌ 对话太过书面化：真实的人在压力下说话有停顿、重复、语序混乱

