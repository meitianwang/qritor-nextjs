### 常见写作错误

1. **推理场景过于冗长**：主角内心独白推理过程占据整章，读者的注意力在中途流失。控制在每个推理阶段最多1/4页的连续内心戏，然后切换到行动
2. **"聪明"靠信息优势而非思维优势**：主角比其他人聪明，只是因为他知道一些别人不知道的信息，而非因为他分析信息的方式不同
3. **对话中直接说出推理结论**：人物在对话中把所有推理用"解说式"台词交代，失去了叙事的展示感
4. **气氛渲染过度**：每一个章节都充满恐惧气氛的形容词，读者对此产生免疫，真正需要恐惧感的场景反而没有效果
5. **被动的主角**：主角总是在接收信息，从不主动推进调查，只是在等待线索上门
6. **法医/专业术语堆砌**：大量专业术语不能替代推理叙事，专业细节只有在推动推理或营造真实感时才有价值
