### 二、犯罪现场描写

#### 初入现场的三段式结构

**第一段（感官冲击）**：用1-2句话给出第一印象
- 不是"他走进了犯罪现场"
- 而是：某一个感官细节最先抵达（气味、异常的安静、温度）

**第二段（空间扫视）**：系统性描述空间
- 从大到小：整体空间 → 区域 → 关键物品
- 每一个被描写的物品都应该有叙事功能（是线索、是氛围营造、或是误导）
- 避免"流水账式"的全面描写，选择性放大重要细节

**第三段（异常聚焦）**：
- 建立"正常感"后，指出一个或多个"不对的地方"
- 不要立即解释这个"不对"意味着什么（制造悬念）

**关于血腥描写的度**：
- 目的是犯罪现实感，而非感官刺激
- 法医视角的描写通常是专业的（描述伤口的特征来推断作案工具），而非渲染痛苦
- 情绪性描写应集中在现场如何影响主角，而非现场本身的恐怖

