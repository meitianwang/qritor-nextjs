### 核心场景写法

#### 复查录像场景

这是赛博灵异最核心的场景类型，写法决定了整个故事的成败：

**准备状态描写**：
主角打开设备 App，选择日期和时段——这个操作本身是普通的日常行为，不需要渲染。写的是「手指滑过屏幕，选中了昨晚」，不是「颤抖的手指」。

**正常部分略写**：
录像的大部分内容是正常的，只需要一两句带过（「客厅录像显示，22:35 我关上了客厅的灯，之后一直是空旷的客厅」）。

**异常出现的精确描写**：
用时间戳精确定位，用画面内容的精确描述代替情绪渲染：
「3:17:43，客厅摄像头的画面里，我的背后站着一个人。」
不要：「突然，恐怖的画面出现了……」

**主角的直接反应**：
不要写内心的恐惧，写行为——主角把录像暂停了，主角把录像回放了，主角截图了，主角把亮度调亮想看清楚细节。行为描写比情绪描写更有力量。

**技术分析过程**：
主角开始分析——他检查是否是压缩失真，他查看其他角度的摄像头，他比对时间戳。这个过程写得越详细，「技术解释最终失败」的冲击力就越大。

#### 音箱自言自语场景

**时间精确化**：
「3:22:11，我的手机收到了推送：【客厅音箱已激活】」

**内容的双重性**：
音箱说的内容必须同时满足两个条件：在技术上有可能是误触发（可以解释），在情境上与主角当时的处境有精准的关联（难以解释）。
「音箱播放了一段广播：『……天气预报显示，今晚零点至凌晨四点，该地区有大雾预警……请注意安全。』我不记得设置过任何定时播报。」

**沉默的处理**：
音箱说完后，写短暂的沉默。主角没有立刻行动，他在等——等什么？他自己也不知道。沉默本身是叙事的。

#### 扫地机器人行为异常场景

**轨迹记录的叙事价值**：
不要只描写机器人「在那里停着」，而是调出 App 里的历史轨迹记录——一张图比一段描述更有冲击力：
「App 里的清扫热力图显示，整个客厅都被覆盖了，但卧室靠窗的那个角落——大约 0.8 平方米的范围——从三个月前的某一天起，就再没有被清扫过。」

**机器人的主动性**：
写机器人接近那个区域、停止、原地旋转、绕开的完整过程——精确到秒数（「在该区域边缘停止了 3 秒，然后向左偏转 90 度，绕道返回」）。技术性描述让机器人的「回避行为」更令人不安，因为这像是「一个被编程过的回避」。

