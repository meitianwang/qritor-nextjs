### 核心写作原则：「不渲染，只陈述」

赛博灵异最忌讳的写法：用情绪化语言描述设备异常。

**错误示范**：
「我颤抖着查看录像，恐惧在我心里疯狂涌动，那个可怕的身影出现了，我几乎要尖叫出来……」

**正确方向**：
「我把录像倒到了 3:17:39。画面里只有我，我正在朝卧室走去。3:17:41，我到达卧室门口，手伸向门把。3:17:43，画面里我的背后出现了一个人形轮廓，站在客厅中央。3:17:44，我关上了卧室的门，客厅的画面里只剩下那个轮廓。录像持续到 3:17:58，那个轮廓没有移动，然后摄像头画面变成了黑屏。」

**原则核心**：时间戳、帧数、具体动作、技术状态——用记录的语言代替情绪的语言。主角的恐惧通过「他做了什么」来体现，而不是通过「他感到了什么」来直接叙述。

