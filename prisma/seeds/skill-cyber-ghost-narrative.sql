### 理性崩塌五阶段的语言特征

主角的理性崩塌不是一次性的，而是五个阶段中语言风格的逐渐转变：

**阶段一：完全理性（设备是工具）**
- 语言特征：主动句、技术术语、第一人称主导
- 典型句式：「我检查了设备日志，发现了以下异常……」「根据技术参数，这个读数表明……」
- 关键词：「检查」「分析」「排查」「应该是」「可能是」

**阶段二：理性动摇（解释在减少）**
- 语言特征：出现「但是」连接词，排查结论开始使用「无法确认」
- 典型句式：「我更新了固件，重启了设备，但那个角落仍然……」「技术上来说这不应该发生，但日志显示……」
- 关键词：「但」「仍然」「还是」「说不通」「无法确认」

**阶段三：接受不确定性（不再强求解释）**
- 语言特征：出现条件句和模态动词，主角开始用非技术语言描述技术现象
- 典型句式：「我不知道是什么原因，但每次音箱在那个时间开口，我都会……」「如果摄像头记录的是真实的，那意味着……」
- 关键词：「如果」「那意味着」「我不知道」「某种」「说不清楚」

**阶段四：恐惧语言入侵（技术词汇开始负载情感）**
- 语言特征：技术词汇开始带有情绪色彩，出现拟人化描述
- 典型句式：「它又在盯着那个角落了」（摄像头）「音箱知道我在说什么」「机器人在回避那里」
- 关键词：「它」（指代设备）、拟人化动词（知道/知晓/选择/回避/等待）

**阶段五：认知框架重建（接受了「有些东西无法被技术解释」）**
- 语言特征：陈述句重新变得简洁，但内容已经与阶段一完全不同
- 典型句式：「我已经不再找技术解释了。」「摄像头记录到的是真实存在的东西。」
- 关键词：「已经」「不再」「就是」（接受的确定性，与阶段一的技术确定性形成对比）

