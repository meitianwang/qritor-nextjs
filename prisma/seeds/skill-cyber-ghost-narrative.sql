### 叙事节奏管理

赛博灵异的节奏控制与其他类型不同：**慢是恐惧，快是崩塌**。

**慢节奏段落**（排查、分析、等待）：
- 长句，技术描写详尽
- 时间感精确（时间戳、持续时长）
- 主角的理性思维过程完整呈现

**快节奏段落**（异常发生时、心理防线崩塌时）：
- 短句甚至断句
- 去掉技术词汇，保留最核心的信息
- 「灯亮了。」「音箱在说话。」「那个角落的东西……」

**节奏切换的叙事信号**：
当叙事从慢切换到快，这本身就是一个信号——读者会感受到「理性分析结束了，现在进入了主角无法用理性处理的时刻」。

