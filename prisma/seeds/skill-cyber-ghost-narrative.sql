### 常见写作错误

1. **过早使用恐惧词汇**：「恐惧」「害怕」「颤抖」出现在故事前三分之一，提前消耗了读者的恐惧感知阈值
2. **用情绪代替证据**：写主角有多害怕，而不是写设备记录了什么——读者的恐惧应该来自于「我也看到了这条记录，我也无法解释」，而不是「主角说他很害怕所以我也害怕」
3. **技术细节错误**：使用不符合真实设备工作原理的技术描述——这会让熟悉智能家居的读者跳出沉浸感
