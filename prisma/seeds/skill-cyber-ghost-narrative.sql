### 设备异常场景的三层叙事结构

每个设备异常场景应该包含三层：

**第一层：技术事实层**
只陈述设备记录显示的客观数据，不加任何解释和情绪。
- 时间戳
- 设备状态变化
- 可量化的数据（温度/亮度/声音分贝）
- 与前后时段的对比（「这是该设备在过去三十天内第一次出现的读数」）

**第二层：主角解释尝试层**
主角在技术事实面前的理性化反应——他的大脑在找解释。这一层可以写出主角的思维过程，但应该是逻辑性的排查，而不是情绪性的反应。
- 「可能是硬件故障」→ 检查设备，排除或确认
- 「可能是软件 bug」→ 查固件版本，排除或确认
- 「可能是我记错了」→ 比对其他记录，排除或确认

**第三层：剩余张力层**
所有技术解释都检验过之后，还剩下什么无法解释的部分。这一层不给出结论，只是呈现残差。
- 「但这解释不了为什么同一个角落连续七天出现同样的数据」
- 「但这解释不了为什么摄像头在最关键的那 7 秒变成了黑屏」

**三层结构的叙事价值**：第一层建立证据的真实感；第二层让读者参与排查过程，感受理性的努力；第三层让读者意识到理性排查之后仍然有无法解释的部分，这个「残差」才是真正的恐惧来源。

