### 监控盲区的设计

**盲区是叙事的黄金区域**，有两种叙事价值：

**「已知安全区」盲区**：
- 没有摄像头的浴室、衣帽间
- 功能：制造「主角唯一确定安全」的空间
- 叙事应用：当主角意识到盲区是唯一的安全区，他与设备之间的关系就逆转了——他开始**回避**自己的设备

**「未知危险区」盲区**：
- 摄像头死角（楼梯转角/床底/特定角度的走廊）
- 功能：主角知道某个区域没有摄像头覆盖，但他不知道那里发生过什么
- 叙事应用：「如果摄像头覆盖了那里，我们会看到什么？」的悬念永远悬而未决

