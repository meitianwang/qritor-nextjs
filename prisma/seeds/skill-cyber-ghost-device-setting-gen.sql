### 异常触发条件的规律性

**完全随机的异常** vs **有规律的异常**：

| | 叙事效果 | 适合的恐惧阶段 |
|--|---------|------------|
| **随机异常** | 更令人不安（无从防范），但难以「证明」 | 故事前期 |
| **时间规律** | 「每晚2:47」——倒计时式恐惧 | 故事中期 |
| **空间规律** | 「只在这个房间」——特定区域的诅咒感 | 故事中期 |
| **行为触发** | 「主角做了X就会发生」——被观察/被回应的恐惧 | 故事后期（最深层） |

**最强组合**：从随机异常开始，读者和主角都无法预判；中段主角发现了规律；后段主角意识到规律意味着「它在回应我的行为」——恐惧从被动升级为交互式。

