### 设备互联架构与「系统恐惧」

当设备独立时，每一个异常都是孤立的——可能是单设备故障。
当设备互联时，多个设备同时或先后出现相关联的异常，单设备故障的解释开始崩塌。

**互联恐惧的最佳结构**：

```
设备A检测到异常（动作/声音）
  ↓ 触发联动
设备B收到联动（摄像头开始录制）
  ↓
但设备B录到的内容，与设备A的触发原因不匹配
```

示例：「门铃动作侦测触发，照理自动开启室内摄像头录制。门铃的触发时间是 2:47。室内摄像头的录像从 2:47 开始，但第一帧画面里，客厅的椅子歪了——没有人进来，但椅子在录像开始的第一帧就已经歪了。」

