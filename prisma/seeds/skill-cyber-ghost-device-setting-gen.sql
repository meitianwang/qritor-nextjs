### 常见错误

1. **所有设备都是摄像头**：失去了设备多样性带来的恐惧维度多样性
2. **没有监控盲区**：全空间覆盖的监控失去了悬念，主角总是能看到发生了什么
3. **设备联动太机械**：A触发→B录制→A和B显示相同内容，没有矛盾感
4. **设备太多导致混乱**：超过8个设备时，读者很难记住哪个设备出现了哪类异常，恐惧感被稀释
5. **忘记设备的「正常功能」**：设备的恐惧来自于「它本来是用来让我更安全的，但现在……」，不描述正常功能就失去了这种反转感',
