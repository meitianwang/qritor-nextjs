- **局限性**：限制了没有孩子的主角
- **特殊叙事功能**：「孩子能看见大人看不见的东西」的民俗信仰让这个设备的异常有额外的文化恐惧层

#### 智能灯光
- **核心恐惧权限**：环境控制——灯光的变化直接影响安全感
- **最强恐惧场景**：「不是全部熄灭，而是只有某一个房间的灯亮了——那个主角根本没去的房间，那个主角一个人住的家里，另一个房间的灯开了。」
- **局限性**：「传感器误触发」是强力解释，需要异常有足够的精确性
- **特殊叙事功能**：灯光异常发生在主角看不见的地方，逼迫主角去确认——而确认本身就是恐惧的一部分

