### 各类设备的「恐惧权限」

#### 监控摄像头
- **核心恐惧权限**：视觉记录——它会「记下」连主角都不知道的事
- **最强恐惧场景**：「主角事后看录像，发现自己一直有什么东西跟着，而当时他完全不知道」。这种「过去时态的恐惧」是摄像头独有的叙事力量
- **局限性**：固定视角，有盲区；压缩和画质提供了「故障」的掩护
- **与其他设备的联动**：与门铃摄像头联动时，可以证明存在在室内外之间移动

#### 可视门铃
- **核心恐惧权限**：门的边界感——它负责「谁在我家门外」这个最基础的安全感
- **最强恐惧场景**：「深夜收到门铃动作侦测推送，打开APP，有人站在门外，面朝里，一动不动。主角跑去开门，什么都没有，回来再看录像，那个人在主角走过来时转身走了——完全正常的行走速度，完全人类的比例，但没有留下任何脚步声录音」
- **局限性**：通常只有一个固定方向；夜视画质差提供了解释空间
- **特殊叙事功能**：门铃视角把入侵从「室内异常」升级到了「它在我家门外」

#### 智能音箱
- **核心恐惧权限**：声音——最亲密的感官，无法「不看」地回避
- **最强恐惧场景**：在完全安静的夜晚，不是骤然响起而是**非常轻柔地**开口——主角甚至需要屏住呼吸才能确认自己听到的不是幻觉
- **局限性**：「唤醒词误触发」是最强的理性化解释，需要设计足够的细节来打破它
- **特殊叙事功能**：音箱能「回应」主角——哪怕只是重复话语或播放相关内容，这种「它在听我说话」的感觉比单纯的「异常出现」更深层地入侵个人感

#### 扫地机器人
- **核心恐惧权限**：行为规律——它用自己的「行动」呈现信息，而非被动记录
- **最强恐惧场景**：「清扫路径地图上有一个固定的绕行区域。那个区域什么都没有，但机器人学习了三周，路径图显示那里一直有一个圆形的禁止区」
- **局限性**：一次异常很难解读，需要积累模式
- **特殊叙事功能**：它是唯一一个「主动在空间中移动」的设备，它知道主角看不到的东西

