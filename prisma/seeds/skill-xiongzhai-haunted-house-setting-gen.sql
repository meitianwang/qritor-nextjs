### 字段生成指南

#### 房屋类型

不同类型的房屋对恐惧氛围有决定性影响：

| 房屋类型 | 核心恐惧逻辑 | 叙事优势 |
|---------|---------|---------|
| **高档住宅小区普通户型** | 高档环境与低廉价格形成最强烈的反差；邻居的正常生活和主角的异常处境对比 | 最经典设定，"为什么只有这间房这么便宜"是天然悬念 |
| **市中心老公寓** | 年代感强，隔音差，楼道里也有恐惧空间；房子本身就有历史厚度 | 前任租客的痕迹更容易保留，调查线更丰富 |
| **别墅或独栋** | 空间大→孤立感更强；主角没有"楼上楼下都有人"的安慰 | 适合多空间探索，每个房间都是独立恐惧单元 |
| **城中村改建公寓** | 混乱的人口流动掩盖死亡历史；周边环境本身也有压迫感 | 信息更难查，调查线更复杂；房东往往更神秘 |
| **学区房** | 反差感极强（家长们的日常与这间房的异常）；邻居的孩子作为叙事元素 | 主角"为了孩子教育"的额外动机，离开成本更高 |

#### 租金反差

租金异常是凶宅流最重要的"初始诡计"——它让主角走进了这间房：

**设计要点**：
- 反差越大，"为什么"的悬念越强
- 但要给主角一个"我选择忽视这个红旗"的理由（实在穷、急需住处、理性主义者认为迷信不可信）
- 最有效的设定是让主角知道"这里便宜是因为是凶宅"，但以为自己可以接受——而他无法接受的程度远超想象

**租金反差类型的叙事含义**：
```
同地段价格的一半以下
→ 明显异常，但可解释为"凶宅折价"，主角进入时已有心理准备（这种准备完全无效）

几乎白给（象征性收费）
→ 房东不在乎钱——他/她在乎的是有人住进来；动机悬念最强

免费住但有奇怪条件
→ 条件本身就是规则框架的开端（"不要开那扇门"、"每周五晚上不要在家"）

价格正常但装修异常豪华
→ 主角并非贪便宜，是被豪华装修吸引——恐惧发生在"意外之中"，代入感更强

中介反复压价主动推荐
→ 中介知道但不说；中介成为信息层的关键人物
```

#### 外部印象

主角第一次看到这间房时的感受——**外部印象要建立一种表面正常甚至令人向往的感觉，恐惧来自后来**：

设计原则：
- 第一印象要有具体的、令人心动的细节（光线好、装修新、小区绿化漂亮）
- 同时植入一个"轻微不对劲"（管道有异味、某面墙的墙漆颜色略深、阳台视野很好但下面是……）
- 主角选择关注前者，忽视后者——这是主动的认知偏见，读者看到了，也理解了

#### 已知死亡历史

主角通过搜索、询问中介、或邻居透露能知道的历史——**这是"表面层"，一定不是真相的全部**：

**信息管理原则**：
- 已知信息要足够具体（一个名字、一个时间、一种死法），让读者感到可信
- 但已知信息应该有明显的"叙述空白"（死因"不明"、"自杀"但细节模糊、租客"失踪"而非死亡）
- 读者会用已知信息拼凑理论，真相要在最后打破这些理论

**典型已知历史层次**：
```
第一层（搜索可得）：
  "这间房曾发生过一起xx事件，两年前，一名租客在此死亡/失踪，案件已结案"

第二层（邻居口述，更具体但也更混乱）：
  "那个人很奇怪，住进来之后就很少出门，最后三天一直有奇怪的声音……"

第三层（只有翻遍房间才能找到的实物线索）：
  日记残页、照片、刻在家具内侧的文字——每一件都比上一层更触目惊心
```

#### 隐藏的核心秘密

**这是整个故事的终极指向，所有恐惧最终都要收束于此**。设计原则：

- **真相要比表象黑暗**：不是"这里死了一个人"，而是"死的不止一个，而且是有人刻意安排的"
- **真相要与主角有关联**：不是"和我无关的历史悲剧"，而是"主角被选中来到这里是有原因的"
- **真相要改变对所有前情的解读**：读者回想第一章，会发现那些"偶然细节"都不是偶然

**核心秘密的常见结构**：
| 秘密类型 | 恐惧来源 | 叙事结构要求 |
|---------|---------|------------|
| **厉鬼有未了冤情，需要主角帮助** | 从恐惧转向悲悯；结局有和解空间 | 前期恐惧，后期共情 |
| **前任租客是被谋杀的，凶手还活着** | 恐惧从鬼转向人；最后的威胁是现实的 | 调查线索指向活人 |
| **这间房是某种"设计"，主角是猎物** | 存在主义恐惧；没有人是无辜的旁观者 | 房东/中介/邻居都有角色 |
| **主角与这间房有前世联结** | 命定感；逃跑也没有用 | 需要主角背景与凶宅历史的交叉设计 |

#### 房东特征

**房东是凶宅流最重要的"活人威胁"**——他/她知道这间房的秘密，却选择继续出租：

**行为特征设计**：

| 房东类型 | 行为模式 | 叙事功能 |
|---------|---------|---------|
| **过于热情型** | 主动提供帮助，询问主角的状态，回复消息极快 | 主角越来越意识到房东的"关心"是监视 |
| **刻意回避型** | 租房后几乎断联，主角问任何关于房子的问题都得到模糊回答 | 房东知道什么——不知道他知道的是线索还是共谋 |
| **神出鬼没型** | 偶尔出现在主角没想到的地方（小区门口、便利店），每次都很"巧合" | 制造监视感，主角不确定是偏执还是真实 |
| **根本联系不上型** | 中介推荐，房东始终不出现；连房东是谁都成谜 | 最大的信息悬念；房东可能根本不是活着的人 |

