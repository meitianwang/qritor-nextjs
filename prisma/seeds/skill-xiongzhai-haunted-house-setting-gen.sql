### 常见错误

1. **凶宅历史太干净**：一次简单的死亡，原因清楚，案件结案——这样的历史缺乏足够的悬念层次
2. **秘密太早暴露**：第三章就揭示了核心秘密，剩下的故事只是"执行已知真相"
3. **房东没有功能**：房东出现一次，之后消失——他应该是整个故事的主动信息方，不是工具性角色
4. **外部印象全是负面**：第一次看房就全是阴郁恐怖——主角为什么要租？好的凶宅设定需要让读者理解"为什么这间房在第一眼看起来值得住"',
