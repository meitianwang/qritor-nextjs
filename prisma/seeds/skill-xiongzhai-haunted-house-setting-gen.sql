### 字段联动关系

- **房屋类型 → 空间模块**：房屋类型决定了可创建哪些空间模块（别墅才有储藏室/地下室，老公寓才有天台等）
- **租金反差 → 故事钩子.开篇悬念**：租金越异常，开篇悬念需要越快解释"主角为什么还住"
- **已知死亡历史 → 角色.前任租客**：已知历史层次决定了前任租客角色的信息密度
- **隐藏的核心秘密 → 厉鬼规则.执念核心**：秘密与执念要互相支撑，厉鬼的执念是秘密的情感核心

