### 常见错误

1. **钩子太弱**：开场案件平淡无奇，读者没有理由继续读下去。至少要有一个令人不安的诡异细节
2. **谜题太简单**：第一章就让读者猜出了凶手，后续的阅读只是等待确认，毫无张力
