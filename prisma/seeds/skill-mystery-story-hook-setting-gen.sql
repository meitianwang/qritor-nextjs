### 字段生成指南

#### 故事钩子（核心钩子句）

**格式**：一到两句话，涵盖三个要素：
- **案件的诡异之处**：是什么让这个案件不寻常（密室、完美不在场证明、死亡的象征性、连环手法）
- **主角面临的核心困境**：不只是"查清凶手"，而是更复杂的处境（怀疑熟人、被体制阻碍、自身与案件有隐秘关联）
- **隐含的更大问题**：一个让读者感到"背后有事"的细节

**三类有效钩子模式**：

| 模式 | 核心张力 | 示例方向 |
|------|---------|---------|
| **不可能犯罪** | 凶案在逻辑上无法发生，但它发生了 | 密室死亡、监控下的谋杀、完美不在场证明 |
| **熟人之谜** | 主角认识受害者或嫌疑人，真相的代价是感情 | 昔日同事、家族旧案、旧恋人成为嫌疑人 |
| **系统性黑暗** | 单一案件只是冰山，背后是体制性的恶 | 连环案件指向同一源头、权贵干预、受害者群体有规律 |

**避免的写法**：
- ❌ 过于平淡的开篇（"某某死了，主角去调查"，缺乏诡异感）
- ❌ 一开始就全部交代（钩子应留悬念，不是摘要）
- ❌ 悬念全靠信息隐瞒（读者能感受到被人为隐瞒，会产生欺骗感；好悬念是"信息都给了，但你没想到那个可能性"）

#### 核心谜题

核心谜题是这部小说区别于其他同类作品的**灵魂问题**。它不只是"谁是凶手"，而是一个更深层的认知挑战。

**四类核心谜题结构**：

| 结构 | 核心问题 | 适合的故事类型 |
|------|---------|--------------|
| **How Done It** | 凶手是如何在不可能的条件下完成犯罪的？ | 密室推理、技术性谋杀 |
| **Why Done It** | 已知凶手是谁，但动机比看起来复杂得多 | 人性探索类、动机反转类 |
| **Who Done It** | 经典的"嫌疑人群像"，最终揭晓谁是凶手 | 封闭空间推理、多嫌疑人类 |
| **What Done It** | 案件本身的定性存疑（是谋杀吗？是意外？是自杀？） | 司法悬疑、证据解读类 |

最强的谜题往往叠加两种结构（如：How Done It + Why Done It = 知道手法但动机藏得更深）。

#### 开篇事件

开篇事件设计原则：
1. **立刻进入案件**：悬疑推理的节奏要求快速进入核心矛盾，前5章最迟建立核心谜题
2. **第一个案件≠最重要的案件**：开篇案件是引子，真正的大阴谋在后面
3. **埋下矛盾的种子**：第一个案件中有某个细节，读者在结尾才会明白它的真实含义
4. **主角登场有故事**：不要让主角毫无背景地出现，他进入这个案件的方式本身就是性格的展示

**开篇案件与大阴谋的四种关联方式**：
- **直接触发**：第一个案件的受害者本身是阴谋的参与者或知情者
- **边缘案件**：第一个案件看似无关，实为大鱼的试探或清场
- **历史重现**：第一个案件唤起了一个未解的旧案，旧案才是根源
- **意外闯入**：主角本来调查普通案件，线索意外指向了更大的秘密

#### 读者预期

读者预期是你对读者做出的承诺。悬疑推理读者进入这个类型，通常有以下几种预期：

| 预期类型 | 对应的写作承诺 |
|---------|--------------|
| **烧脑的智识满足** | 线索是公平的（读者理论上能自己推理出来），推理过程精密严谨 |
| **压抑的氛围体验** | 社会暗面的沉重质感，案件背后是对人性的深度解剖 |
| **节奏的紧张刺激** | 持续的悬念累积，每章结尾都有新的钩子 |
| **角色的情感共鸣** | 受害者不是工具，嫌疑人是有血肉的人，主角的成长有代价 |

**关键原则**：明确自己的读者预期并坚守它。写烧脑推理的书不要突然转向情感向；写人性拷问的书不要把逻辑漏洞堆砌成谜题。

