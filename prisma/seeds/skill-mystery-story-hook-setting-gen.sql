### 字段联动关系

- **核心谜题 → 阴谋框架.核心秘密**：谜题的最终答案应与大阴谋的核心秘密相呼应
- **开篇事件 → 主角设定.推理方法论**：第一个案件应该是展示主角独特侦查方式的最佳舞台
- **读者预期 → 文风设定.悬疑氛围**：两者必须一致，文风是实现读者预期的工具

