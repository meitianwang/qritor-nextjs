### 字段生成指南

#### 故事钩子（核心钩子句）

有效的末班车钩子需要同时包含：
1. **日常性**：主角的处境要真实可信（加班、买醉、错过时间）
2. **第一个不对**：上车后最初的一个细节让读者感到"这辆车有问题"
3. **被困的意识**：主角意识到无法下车，或者即将意识到

**三种经典钩子结构**：

| 结构 | 核心张力 | 适合的氛围 |
|------|---------|---------|
| **"等等，这是哪路车"** | 主角上车后才意识到这辆车不在路线表上 | 渐进式恐惧，初期信息克制 |
| **"我应该在第三站下车，但是……"** | 主角试图下车，门不开，或站台不存在 | 即时困境，张力前置 |
| **"车上只有我一个人，但后来不是了"** | 空车变成满车，乘客不像人 | 孤独→不孤独，对比恐惧 |

**写作要点**：钩子一句话，但必须同时完成"我理解主角的处境"和"我感到不对劲"两件事。

#### 上车契机

上车契机决定了**主角为什么在那个时间、那个地点**。好的契机需要：
1. **可信的现实理由**：读者必须相信"换作是我，我也可能这样"
2. **轻微的过失感**（可选但有效）：主角因为某个小错误错过了正常班次，上了这辆车——这种轻微的"自己造成的"感觉会加重心理重量

**各契机的叙事特点**：

| 契机类型 | 叙事优势 | 注意事项 |
|---------|---------|---------|
| **加班误点** | 最普通可信，中年社畜读者代入感强 | 需要一个具体的"最后一刻"细节让主角决定等这辆车 |
| **意外错过普通班次** | 命运感强，"差一点点"的遗憾 | 错过的原因要合理，不能太刻意 |
| **强迫或被引导上车** | 恐惧感前置，主角一开始就觉得不对 | 为什么主角没有立刻离开？需要合理解释 |
| **灾难/事故后的逃离** | 强烈的求生本能驱动，戏剧性强 | 容易与末班车本身的恐惧竞争，需要平衡 |
| **梦境或特殊精神状态** | 现实/非现实边界模糊，可以一直不确定 | 容易被读者归结为"只是梦"，需要锚定某个实在的物证 |

#### 第一个诡异信号

这是整篇小说中最重要的一个细节，决定了读者第一次感到不安的时刻。设计原则：

**优秀的第一个诡异信号特征**：
- **细微**：不是司机没有头，而是司机的影子方向不对
- **可疑但可解释**：主角（和读者）第一时间可以用"也许是光线的原因"来说服自己
- **难以忘记**：这个细节会在后面被想起，届时它的真实含义变得清晰

**常见第一信号类型（由弱到强）**：
```
【克制级】
- 车票上的字在微微颤动
- 司机的后视镜里没有主角的倒影
- 同排乘客的呼吸声不在胸口位置
- 窗外的路灯全部熄灭，但车内光线没变

【中等级】
- 报站声念了一个地名，主角不认识
- 手机时间停在了上车那一刻
- 旁边的乘客低声说"你不应该在这里"

【强烈级】
- 主角看向出口，门上的安全锁是从外面锁的
- 同排的"乘客"转过头来，有一对眼睛太多
```

**原则**：第一个信号用克制级，后续信号逐步升级。

#### 终点的代价

终点代价的设计对全书的张力有决定性影响：

| 代价类型 | 恐惧核心 | 信息管理 |
|---------|---------|---------|
| **死亡** | 直接，明确，但缺乏想象空间 | 建议搭配"死法不确定"来扩大恐惧 |
| **灵魂被困** | 中国民俗基础强，读者熟悉且有共鸣 | 可以与"比死更糟"形成对比 |
| **被带入冥界** | 给后续剧情更多发展空间 | 可以有主角在冥界的更多冒险 |
| **不知道** | 最强的存在主义恐惧——未知比已知更可怕 | 最后可以揭示，也可以永远不揭示 |

建议：前半部分用"不知道"，让读者和主角一起想象最坏的可能；中段通过某个乘客的命运暗示代价的具体形态。

#### 获救的希望

规则流恐怖有别于纯粹绝望类型恐怖——主角理论上可以活下来，这种"有可能"的感觉是读者继续阅读的动力：

- **希望的来源要可信**：不能是"突然有人告诉他规则"，除非这个"有人"本身是一个谜
- **希望的代价要清晰**：活下来不是免费的（可能需要做某件事、付出某种代价）
- **希望不能太早确定**：如果第一章就明确知道怎么下车，剩下的只是执行，张力消失

