### Synopsis Structure

The synopsis for a Vengeance Driven novel must do three things simultaneously:
1. Make readers feel the wound
2. Make readers trust the protagonist as a guide into darkness
3. Make readers afraid to look away

**Paragraph 1 — The World Before and the Wound (2-3 sentences)**
Establish the protagonist before the betrayal in one sentence. Then deliver the wound. Keep it specific and brief. The wound should feel like a physical impact.

> [Name] was a [who they were]. When [the specific act of betrayal], [what was destroyed in one phrase]. [What they became in one phrase.]

**Paragraph 2 — The Mission (2-3 sentences)**
Establish the scope of the revenge and what it will require. Hint at the cost. Create the central tension: the reader should understand both the justice of the mission and its danger.

> [Time later], [Name] has spent [what they sacrificed] becoming [what they are now]. [Primary target] is [their current status and why they seem protected]. To reach [them/it], [Name] must [the challenge — and what it costs].

**Paragraph 3 — The Hook (1-2 sentences)**
The promise to the reader. What are they here to see? This should create anticipation and dread simultaneously. End with something that implies the novel''s central question.

> [The specific promise of the reckoning]. But [the cost, the complication, or the question that will remain after the last name is crossed off the list].

**Length**: 80-120 words total. Tight. No comfort.

---

### Common Errors

- ❌ The title announces the genre: "Revenge," "Vengeance," "Retribution" as a single-word title. These are genre labels, not titles.
- ❌ The title is a character name without context: "Aldric" alone means nothing. "The Last Aldric" or "What Aldric Built" implies a story.
- ❌ The synopsis mentions the "journey" or "quest": Vengeance Driven fiction is not a journey. It is a hunt. The language of the synopsis should reflect this.
- ❌ The synopsis tries to make readers like the protagonist: Readers should fear them, be complicit with them, and not be able to look away. "Likeable" is the wrong target.
- ❌ The synopsis gives away the cost: The central complication or the price the protagonist will pay should be implied, not stated. Leave room for dread.
