### 一、乘客命名

#### 人类乘客（主角和普通人）

**命名策略**：使用完全普通的现代中文人名或称谓。

- **主角**：通常只在故事中用「我」（第一人称），或「他/她」（第三人称），不需要名字。如果需要名字，选择极其普通的名字（「李凯」「张明」「小刘」）。
- **普通人乘客**：如果主角认识他们，用普通名字；如果不认识，用外貌特征描述（「穿红衣的女人」「看起来三十多岁的男人」）。

**避免**：名字有特殊含义（「命苦的人」叫「苦命」）、名字过于文艺（「沈暮云」「白苍生」）——这些名字会破坏都市现实感。

#### 非人乘客

**命名策略**：**大多数非人乘客不应该有名字**。

主角对他们的称呼应该基于：
- 外貌特征：「穿校服的小孩」「戴帽子的老人」「后排的那个」
- 位置：「三排的」「靠窗的那个」「站着的」
- 某个细节：「手很长的乘客」「从来不眨眼的女人」「一直翻包的」

**为什么不给名字**：给非人存在取名意味着主角在「认识」他们，而「认识」会减弱恐惧感。保持描述性称呼让他们始终是「陌生的异物」。

**例外**：如果某个非人乘客在故事中有重要的叙事角色（如边缘状态乘客，主角与他/她有真实互动），可以给一个普通的名字——但这个名字在故事中应该有叙事意义（它可能是这个存在最后的「人的部分」）。

