### 二、站点命名

#### 现实风格的站名（故事前期站点）

**命名原则**：模仿真实公交站名的命名逻辑

中国城市公交站名的常见格式：
- 「路口型」：[路名]+[路口/十字路口]，如「中山路口」「建国路十字」
- 「区域型」：[区域名]+[地标类型]，如「虹桥商圈」「南站广场」
- 「地标型」：直接用地标名称，如「市政府」「人民广场」「火车站」
- 「社区型」：「XX小区」「XX新村」「XX花园」

**叙事技巧**：选择真实感强、读者可能有印象的站名格式（哪怕虚构城市），让主角确认「这是我认识的地方」——然后用下一段描写颠覆这个认知。

#### 变形站和异常站的命名

**渐变策略**：

```
正常站：「建国南路」
轻微变形：「建国南路（旧）」「南路口建国」（语序变了）
明显变形：「建国路 [某个不认识的字]」「南路-3号」（不应该出现的字符）
完全陌生：「[空白]」「回场」「--------」（路牌内容无法阅读）
不存在的站：「[不认识的汉字]」「此处无站」「XX（撤销）」
```

**终点站命名**：

终点站的名字不应该是恐怖的，而应该是**反差感强烈的**：
- 过于普通的名字：「终点」「回场」「存车处」
- 看起来是日常地点的名字：「北郊公墓」「旧城区」（这是真实公共汽车路线可能有的终点，普通中带着不安）
- 完全无法解读的名字：空白、乱码

