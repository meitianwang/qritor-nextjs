### 四、车辆本身的命名

**核心建议：不要给末班车本身取名**

给这辆车取一个「正式名称」（「死亡班车」「冥界快线」）会破坏整个类型的美学——这辆车之所以可怕，就是因为它看起来像一辆普通的公共汽车。

**可以使用的称呼方式**：
- 用路线号称呼：「那辆 57 路」「那辆没有编号的车」
- 用时间称呼：「末班车」「那辆深夜的车」
- 用模糊指代：「那辆车」「它」（在故事已经建立语境后）

**如果故事背景中这辆车有「传说」**（即它在都市传说中有人提过），可以用传说中的民间称呼：「那辆收魂车」「夜班鬼车」——但这应该是故事中其他角色的说法，不是叙事者的正式命名。

