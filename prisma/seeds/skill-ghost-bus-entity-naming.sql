### 三、道具命名

#### 命名原则：能不命名就不命名

规则流道具的最佳状态是**没有专属名称**，用功能性描述代替命名：

- ❌ 「冥界护符」「驱邪令牌」——过于「名字化」，失去了日常物件的恐怖美学
- ✓ 「那张旧车票」「上面写满字的笔记本」「他给我的那枚硬币」——描述性称呼保持道具的「来历不明感」

**例外情况**：如果道具有明确的制造者或历史（例如某人专门为末班车准备的道具），可以给一个**功能性的简单名称**，但应该是使用者的自称（「我姑且叫它''遮眼咒''吧」），而不是世界内的正式名称。

#### 日常物件的命名方式

当道具本身是日常物件（车票、手机、旧书）：
- 直接用物件本身的名称：「那张票」「手机」「那本笔记本」
- 用来源区分（「他留下来的那张票」而不是「那张神圣的票」）
- 用状态区分（「越来越黑的那枚硬币」而不是「消耗中的护符」）

