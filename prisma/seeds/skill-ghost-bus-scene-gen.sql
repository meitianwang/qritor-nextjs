- 位置建议：出现在故事前期，营造「下车尝试」的机会

**变形站（现实站点的扭曲版本）**：
- 叙事功能：中场核心站点，传达「世界已经不是原来的世界」的转折
- 设计要点：站名和现实站相同或相近，但站台的细节有难以解释的错误（广告牌上的文字变了/建筑的比例不对/月台上站着的人影太多太静）
- 位置建议：故事中段，标志着「无法回头」的心理节点

**废弃站（早已停用的站点）**：
- 叙事功能：过去的痕迹，可以作为线索的容器（前人留下的警告、规则的历史证据）
- 设计要点：这个站在现实中已不存在，但这辆车依然停靠。站台荒废但有人曾经在这里的痕迹——一张旧车票、一双鞋、一段文字
- 危险级别：适合设计为「平静但信息量大」，是主角发现关键线索的地点

**不存在的站（路线表上没有这一站）**：
- 叙事功能：规则体系异常的信号，同时是最高张力的站点之一
- 设计要点：这一站没有名字，或者名字是乱码/不认识的字。停靠之前车没有减速，主角甚至不确定这算不算一次停靠
- 危险级别：高度危险到致命

**折返站（下车的唯一机会）**：
- 叙事功能：「出口」的所在，也是故事最关键的节点之一
- 设计要点：通常有某种「正常感」作为掩护（最不显眼的站反而是出口），但有一个需要主角用智慧才能识别的标志
- 位置建议：故事中后段，让主角先错过，再通过线索认出

**终点站（故事的最终目的地）**：
- 叙事功能：如果主角到达终点，故事就结束了（或者进入完全不同的故事阶段）
- 设计要点：终点站的描述应该是**未完成的**——让读者感到终点站是一个「只有到达才会知道全貌」的地方
- 处理原则：保持神秘，不要提前揭示太多

