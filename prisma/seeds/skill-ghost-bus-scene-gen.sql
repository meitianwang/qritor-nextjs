### 每个站点应该完成的叙事任务

一个优秀的站点描写需要完成至少两项任务：

**必选任务（至少一项）**：
- 提供一条新信息（关于规则、世界观、其他乘客或下车条件）
- 改变乘客构成（有乘客上来，或有乘客下去——不一定是「下车」）
- 测试或触发一条规则（主角在这一站面临规则的考验）

**加分任务**：
- 改变主角与某个乘客的关系
- 提供可以在后续使用的道具或线索
- 窗外的景象传达某种难以言说的信息

**避免的站点写法**：
- 车开到了某站，停了一下，又开走了——什么都没发生
- 每一站都有非常相似的描写（「又到了一站，同样昏暗的站台，同样的沉默」）

