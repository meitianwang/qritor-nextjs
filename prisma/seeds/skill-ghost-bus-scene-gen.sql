### 站外景象的设计

窗外的世界是末班车恐惧的重要维度——它既是「外部世界的信号」，也是「我们已经走了多远」的刻度：

**现实城市的深夜景象**（故事前期）：
- 空旷的街道、没有行人的人行道、远处的楼宇灯光
- 叙事功能：「现实就在窗外」的假象，让下车尝试看起来合理
- 细节建议：可以有一辆停着的出租车，一个快要关门的便利店——越「正常」的现实细节，越强调主角和现实之间那块玻璃的距离

**荒凉旷野**（故事中期）：
- 公路两侧是没有任何光源的旷野，路上没有其他车
- 叙事功能：「已经离城市很远了」，外部救援的可能性降低
- 细节建议：避免用「一片漆黑」描述，这太通用。旷野应该有某种「异样的平静」（「草不动，即使能感觉到车在开」）

**无法辨识的地形**（故事后期）：
- 窗外是主角完全无法判断的景象——不是任何已知的地方
- 叙事功能：「已经离人间很远了」的确认
- 细节建议：可以利用建筑的异常（「建筑物的灯光排列方式不符合任何正常的城市逻辑」）或自然的异常（「天空的颜色不是深夜应该有的颜色」）

**对称或循环的景象**（特殊效果）：
- 主角看到窗外经过的景象好像见过——像是在循环
- 叙事功能：「时间/空间可能出了问题」，加深迷失感

