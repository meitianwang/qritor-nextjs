### 站点的危险等级设计

不是每一站都需要是高危站——节奏的变化本身就是氛围的一部分：

```
平静站 → 轻度异常站 → 平静站（假平静）→ 高度危险站 → 平静站（代价后的喘息）→ 致命站
```

**「假平静」的运用**：
在高度危险站之后安排一个表面平静的站，但用细节暗示「平静是假象」。读者已经知道这辆车不安全，平静本身就令人不安。

