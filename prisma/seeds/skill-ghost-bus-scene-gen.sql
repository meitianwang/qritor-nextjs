### 站点与规则体系的联动

**规则变化时刻**：
某些站到达时规则会发生变化，这种变化应该服从规律（不能是随机的）：
- 过了某一站，某条规则失效（但主角不知道这个规律）
- 过了某一站，新的规则生效
- 主角在某站的行为（违规或遵规）影响了之后的规则状态

