### 设备互联：系统性恐惧的构建

单个设备的异常可以用技术故障解释；多个设备**同步异常**才开始产生系统性恐惧。

**互联设计原则**：

**时间同步型**：
多个设备在同一时刻（或极短时间差内）出现异常——「3:17:42，门铃被触发；3:17:45，走廊灯被激活；3:17:51，音箱说了一句话」——这个时间序列告诉主角，发生了一次「进入」的动作序列，但录像里没有任何人。

**因果连锁型**：
设备 A 的异常触发了设备 B 的反应——「扫地机器人绕开了某个角落，那个角落正好是门铃摄像头的盲区，而音箱在同一时间开始播放一首主角不认识的歌」。

**记忆互证型**：
分别查看单个设备的日志时，每个设备的记录都「在技术上可以解释」；但将多个设备的日志对比后，出现了不可能同时合理的情况——「这两件事不可能同时是技术故障」。

