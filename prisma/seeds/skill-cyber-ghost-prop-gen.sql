#### 可视门铃

**技术层**：
- 动作触发机制（PIR 传感器 + 视频分析的双重触发）
- 推送延迟（真实设备有 1-3 秒延迟——看到的不是「现在」）
- 红外夜视范围（通常 3-5 米）

**叙事层恐惧权限**：
- 「谁触发了门铃」：门铃被触发意味着有东西经过了传感器范围
- 「没有触发」：门铃没有反应，但主角发现门口有痕迹——要么是盲区，要么是设备选择了不记录
