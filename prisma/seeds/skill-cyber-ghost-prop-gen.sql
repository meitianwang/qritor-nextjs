### 设备类型与核心恐惧权限

#### 监控摄像头

**技术层**：
- 覆盖角度（100-180°视角是真实设备的范围）
- 分辨率与夜视能力（决定了证据的清晰度）
- 存储周期（通常 7-30 天循环覆盖——「超过存储周期的录像是否真的消失了？」）
- 云端备份状态（是否同步到服务器——本地删除的录像在云端是否还存在？）

**叙事层恐惧权限**：
- 「摄像头拍到了什么」：证据最强的设备，但也因此是最需要被解释的设备
- 「摄像头没有拍到什么」：盲区的形状是固定的——如果异常总是出现在盲区，这是巧合还是选择？
- 「摄像头被遮挡/角度被改变」：不是异常，但意味着有什么「知道」摄像头的位置

**最强叙事设计**：主角复查昨晚的录像，发现有一段时间（3:17-3:24）录像是黑屏，但时间戳在连续跳动——设备在运行，但什么都没有被记录下来。

