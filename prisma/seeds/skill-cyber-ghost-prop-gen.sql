### 设备的历史溯源

设备本身有历史：
- 「这台监控摄像头是上一个租客留下来的」：上一个租客为什么没有带走它？他们经历了什么让他们离开得如此仓促？
- 「这个型号的设备有固件漏洞」：真实存在的技术漏洞（可以被外部入侵），但主角在确认漏洞已修复后，异常仍在继续
- 「购买记录显示这台设备不是我买的」：设备出现在主角家里，但购买记录找不到

