### 设备可信度系数：理性瓦解的量化追踪

**可信度系数**（isDynamic 字段）追踪的是每个设备记录的异常在「技术解释」和「无法解释」之间的位置：

| 可信度等级 | 含义 | 叙事状态 |
|-----------|------|---------|
| 完全可解释 | 有明确的技术原因（硬件故障/软件 bug/用户误操作） | 理性阶段，主角还能自我说服 |
| 大概率可解释 | 可能有技术原因，但概率在下降 | 理性开始动摇 |
| 难以解释 | 穷举所有技术可能性后仍有残差 | 主角开始接受「非技术」可能性 |
| 无法解释 | 在现有物理规律下不可能发生 | 理性防线崩塌 |

**设计要点**：
- 同一设备的可信度系数应该随故事进展**逐步下降**，而不是一开始就是「无法解释」
- 不同设备的可信度系数**不应该同步下降**——一个设备率先突破「难以解释」，其他设备仍在「大概率可解释」阶段，才能构成有层次的张力
- 可信度系数的每次降级应该对应一个**具体的触发事件**（主角找不到技术解释的那个时刻）

