### 常见错误

1. **设备功能超出真实技术边界**：现实中的摄像头不能「记录梦境」，智能音箱没有情绪感知——超出技术边界会破坏赛博灵异的「理性坍塌」美学
2. **所有设备同时出现异常**：系统性同步崩塌失去了「逐步侵蚀」的恐惧层次
3. **没有设计「技术上可以解释」的阶段**：第一个异常就是「无法解释」，主角跳过了理性挣扎的过程，损失了核心叙事张力
4. **忘记了设备的存储/日志特性**：现实中每种设备的数据留存方式不同，日志被删除/覆盖是重要的叙事细节
5. **设备孤立存在，没有互联**：赛博灵异最独特的恐惧是「系统性」，单一设备的异常缺乏这个维度',
