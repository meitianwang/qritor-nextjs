### 伏笔策略

**悬疑推理的伏笔原则**：
所有关键伏笔必须在第一次出现时**不显眼**（混在其他信息里），但在揭示时**完全合理**（回头看必须能找到那个细节）。

**五类核心伏笔**：

| 伏笔类型 | 埋设方式 | 回收时机 |
|---------|---------|---------|
| **物证伏笔** | 作为场景描写的一个细节出现 | 在关键推理时被重新解读 |
| **行为伏笔** | 某人"正常"的行为，后来证明是罪证 | 真凶揭晓时，回溯该行为的真实含义 |
| **对话伏笔** | 某句话被理解为A意思，实为B | 揭示阶段，引用原句重新解读 |
| **缺席伏笔** | 本来应该有的东西不存在 | 中段或结尾揭示缺席的意义 |
| **身份伏笔** | 角色的某个特征暗示其真实身份 | 身份揭示时，读者"我就说嘛" |

**伏笔的密度控制**：
- 前三分之一：主要建立角色和世界，少量物证伏笔
- 中间三分之一：伏笔密度最高，但要分散在信息洪流中
- 最后三分之一：伏笔逐步收回，读者开始感到"所有东西都在到位"

