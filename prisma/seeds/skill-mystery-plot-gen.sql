### 常见错误

1. **中段节奏停滞**：调查展开后，很长一段时间没有新信息，读者感到原地踏步。对策：每3-5章至少有一个新发现，哪怕只是排除一个嫌疑人
2. **假突破不可信**：主角以为找到凶手，但被否定的理由太牵强，读者感到这只是为了延长篇幅
3. **线索在揭示时才出现**：违反公平游戏原则，最应该避免
4. **反转过多导致疲劳**：三层以上的反转需要极精密的设计，否则读者会感到"你随便说谁是真凶都行"
