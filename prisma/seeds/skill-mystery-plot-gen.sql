### 故事线模式

**主线模式（二选一或组合）**：

**A. 串联案件型**（最常见）
- 多个案件有内在逻辑联系，每破一案揭示大阴谋的一片拼图
- 优点：节奏感强，每案有满足感，同时大阴谋有持续吸引力
- 设计要点：每个案件的诡计要有独特性，不能只是"相同手法的重复"

**B. 主案深挖型**
- 一个核心案件从多角度深挖，真相层层剥开
- 优点：人物刻画深度、氛围积累、心理描写空间充足
- 设计要点：需要足够复杂的案件支撑全书，中段不能出现明显的信息停滞

**标准支线配置**：
- **主角个人线**：主角的个人危机（心理创伤、人际关系）与案件互相渗透
- **体制阻力线**：上级压力、内部怀疑、体制障碍，形成持续摩擦
- **大阴谋隐现线**：每隔若干章出现一个与大阴谋相关的细节，让读者逐步感到"事情不简单"

