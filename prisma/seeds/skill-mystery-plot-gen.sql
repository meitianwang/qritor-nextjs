### 事件编排模式

**各阶段的关键事件类型**：

**建立阶段（钩子）**：
- 主角登场事件（展示性格和能力的最佳场合）
- 第一案件发现（第一个诡异细节必须足够不寻常）
- 第一个疑点建立（让读者意识到有人在说谎）

**调查展开阶段**：
- 物证发现序列（每次发现推进一步，但不立即解答）
- 第一轮嫌疑人访谈（建立嫌疑人群像，每个人都有隐瞒）
- 时间线矛盾出现（某个不在场证明被质疑）
- **中段假突破**（主角以为找到了凶手→被证明错误→更深的迷雾）

**迷雾加深阶段**：
- 主要嫌疑人被排除（读者被引向错误方向后，方向骤然改变）
- 新受害者出现（阴谋在升级，封口行动）
- 主角个人受到威胁（阴谋方开始针对主角）
- **误导线索的"解释"**（之前看似关键的线索被证明是误导，理由要令人信服）

**真相逼近阶段**：
- 关键线索重新解读（早期以为是A意思的证据，现在发现是B意思）
- 意想不到的盟友出现（或意想不到的背叛）
- 主角与幕后势力的直接对抗（危险升级）
- **核心反转**（真凶揭晓，或大阴谋的第二层真相）

**对决与揭示阶段**：
- 最终对决（逻辑+情感的双重对决）
- 大阴谋全景揭示（给读者一个完整的图景）
- 主角的个人代价（承担揭开真相的情感成本）

