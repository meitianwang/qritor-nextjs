### 证据的两面性——核心张力的设计

**赛博灵异最独特的叙事张力**：所有异常都可以有技术解释，而这些解释几乎总是比「这是鬼」更合理。

**为每一类异常设计对应的技术解释**：

| 异常类型 | 技术解释 | 解释的有效期 |
|---------|---------|-----------|
| 摄像头残影 | 压缩伪影/帧率问题/红外感光 | 第一次很可信，第三次开始站不住脚 |
| 动作侦测误触发 | 灵敏度过高/风吹窗帘/光线变化 | 一次完全可信，规律性触发时崩塌 |
| 音箱自动播放 | 语音指令误识别/定时任务/固件bug | 对于随机内容可信，对于有意义内容不可信 |
| 机器人异常路径 | 固件更新/传感器故障/磁场干扰 | 单次可信，重复相同模式后崩塌 |
| 灯光自动开关 | 传感器误触发/定时设置/电路问题 | 需要配合「精确的规律性」才能崩塌 |

**解释崩塌的三种机制**：
1. **统计上不可能**：同一时间、同一设备、完全相同的异常重复了足够多次
2. **内容有意义**：异常的内容不可能是随机生成的（音箱播放了只有主角知道的内容）
3. **多设备独立证实**：两个没有联动的设备独立记录到相同的异常

