### 「为什么主角无法被帮助」的四种机制

这是世界规则中最需要精心设计的部分：

**机制一：证据在展示时消失或降级**
- 主角将录像展示给他人，在播放时异常部分出现了画质降级/文件损坏/设备临时断电
- 叙事价值：存在在保护自己不被暴露（如果存在是「数据型」尤其适合）

**机制二：他人能给出更合理的解释**
- 技术支持来检查设备后找到了一个「合理」的原因（但这个原因解释不了所有异常）
- 叙事价值：主角独自面对「我知道那不是故障，但我无法证明」的困境，孤立感加深

**机制三：他人也感知到了，但选择不承认**
- 物业人员/邻居/家人也遇到了类似的事情，但出于各种原因（恐惧/保护主角/有什么要隐瞒的）选择否认
- 叙事价值：孤立变成了「被孤立」，有更深的阴谋感

**机制四：帮助到来，但太晚或代价太高**
- 某个人相信了主角并尝试帮助，但这个帮助行为本身触发了更严重的异常
- 叙事价值：「试图终止故事」反而加速了危机，强化了无力感

