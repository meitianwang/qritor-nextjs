### 科技-灵异接口原理的选择

这是整个世界观的基础假设，一旦确定必须全文自洽：

#### 「设备作为记录媒介」原理
- 假设：摄像头的光学传感器能捕捉到人眼视觉系统无法处理的频率或信号——人眼在进化中过滤掉了某些视觉信息，而摄像头没有这层过滤
- 优点：解释直观，有一定的伪科学可信度；主角可以「用摄像头看」来主动探测
- 约束：存在一直都是可见的（通过设备），它只是之前没有人专门看；存在无法「使用」设备，只能被记录

#### 「电磁信号作为显现条件」原理
- 假设：某些存在需要特定频率的电磁环境才能产生可感知的影响，智能设备的WiFi/蓝牙信号无意中提供了这个条件
- 优点：解释了「为什么以前没有这些设备时没有发生」；主角可以通过断网来减弱（但无法消除）异常
- 约束：断网会让异常减弱但不会消失（信号消失后存在还在，只是影响力降低）

#### 「算法作为镜子」原理
- 假设：智能设备的AI算法在学习过程中接触到了某种「模式」，开始模仿这种模式——设备本身没有被占领，而是算法在无意中「学会了」非人类的行为
- 优点：最具现代科技恐惧感；不需要引入明确的「超自然存在」概念
- 约束：需要一定的AI技术背景来支撑可信度；适合主角是IT背景的故事
- 最强场景：「不是有什么东西在控制设备，而是设备自己学会了这些行为——它们在训练数据里看到了什么？」

#### 「网络作为通道」原理
- 假设：存在可以通过局域网或互联网在互联的设备之间移动，它不依附于物理空间，而是依附于网络节点
