### 科技防御的有效性设计

主角可以尝试的技术手段，以及其有效性：

| 防御手段 | 有效性设计建议 |
|---------|-------------|
| 关闭某个设备 | 短期有效（某个设备的异常消失），但其他设备开始出现新的异常 |
| 全部断电 | 短期内没有异常，重新开机后异常更密集——仿佛「积累了能量」 |
| 断开网络 | 取决于世界规则选择：「网络通道」原理下有一定效果，其他原理下无效 |
| 重置/恢复出厂设置 | 最强的理性期望，但在故事中的效果建议：设备重置后第一次启动时就立即出现异常 |
| 更换新设备 | 新设备第一次出现异常是最强的「这不是设备问题」的证明 |
| 搬离 | 搬离后，存在是否跟随？取决于存在的性质（空间绑定 vs. 人绑定） |

