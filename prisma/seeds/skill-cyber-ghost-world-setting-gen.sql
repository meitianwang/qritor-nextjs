### 常见错误

1. **报警能解决**：主角没有向警察展示录像，或展示后警察没有任何反应，缺乏解释
2. **技术解释太脆弱**：第一次异常就是技术上完全无法解释的，主角没有理性化的空间
3. **世界规则与实体行为矛盾**：世界规则说「存在只能被记录」，但后来实体移动了物体
4. **断网完全有效**：如果断开网络能完全解决问题，主角为什么不一直断网？需要设计「断网代价」
5. **主角获得了太多帮助**：多个可信的目击者、技术专家的确认——这些帮助消解了孤立感，而孤立感是这个类型的核心恐惧来源',
