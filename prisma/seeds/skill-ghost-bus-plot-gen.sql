### 常见错误

1. **规则发现太早，之后没有新内容**：前三分之一就告知了所有规则，之后只是「应用规则」，缺乏新的发现
2. **每一站发生的事情结构相同**：「到了一站，上来一个可怕的东西，主角保持镇定，过了」——第三站开始读者就知道下一站会发生什么
3. **逃脱方法太随机**：主角突然想到一个没有任何铺垫的办法并成功，读者感到「这是作者在帮他」
