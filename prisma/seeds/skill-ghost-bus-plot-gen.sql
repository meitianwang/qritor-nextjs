### 短篇与中长篇的结构差异

**短篇（3000-10000字）**：
- 规则建议 4-5 条，只揭示核心规则
- 只有一次逃脱尝试（或没有尝试，结局更开放）
- 乘客 3-5 个，以氛围为主，不需要完整的角色弧
- 站点 3-5 个，每站聚焦一件事
- 结局可以是「逃脱」也可以是「模糊/开放」

**中篇（10000-30000字）**：
- 规则 6-8 条，有发现-误解-纠正循环
- 两次逃脱尝试，第三次成功
- 乘客 5-8 个，1-2 个有简单的个人弧
- 站点 6-10 个，每站有具体的叙事任务
- 结局需要是「完整的」

**长篇（30000字以上）**：
- 规则体系可以有「版本更新」（某些规则在特定条件下改变）
- 逃脱尝试可以有 4-5 次，每次失败都加深谜题
- 乘客有完整的角色弧，可以是「过去出现过、已经消失的乘客的回溯」
- 站点可以有「重复经过的站」（每次经过略有不同）

