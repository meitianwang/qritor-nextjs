### 规则发现弧的设计

规则发现弧是末班车故事特有的叙事结构，需要精心设计：

**发现-误解-纠正循环**（推荐中长篇）：

```
步骤1：主角观察到异常 → 形成对规则的猜测（不完整或有误）
步骤2：主角基于这个猜测行动 → 猜测被部分证实
步骤3：主角遭遇新情况 → 发现猜测有漏洞
步骤4：主角修正理解 → 新的理解引导下一步行动
步骤5：（循环）
```

**关键节点**：
- 第一次「规则被违反但没有死」：建立「轻微违规有警告」的读者认知
- 第一次「观察到其他乘客违规的后果」：通过旁观学习规则，比自己犯错学效率高
- 「规则之间存在矛盾」的发现：同时遵守两条规则在物理上不可能——这是中段张力的主要来源

