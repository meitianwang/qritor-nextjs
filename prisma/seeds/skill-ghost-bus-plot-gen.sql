### 逃脱尝试序列

**三次尝试原则**：

好的逃脱故事通常包含三次尝试，失败原因各不相同：

**第一次尝试（正面尝试，物理性失败）**：
- 主角尝试直接下车（规则告诉他可以在某站下车，他尝试了但无法离开）
- 失败原因：对规则理解不完整
- 叙事功能：建立「逃脱不是简单的」的认知

**第二次尝试（曲线尝试，逻辑性失败）**：
- 主角发现了某个漏洞，尝试利用它下车（漏洞的利用本身是对的，但触发了另一个问题）
- 失败原因：规则之间的相互制约
- 叙事功能：让读者认识到规则体系的复杂性，提升对最终解法的期待

**第三次尝试（真正的破局）**：
- 主角整合了所有的理解，找到了真正可行的方法
- 成功条件：回溯看，所有的线索和铺垫都指向这个解法
- 叙事功能：「恍然大悟」的读者满足感

