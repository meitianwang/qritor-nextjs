### 内容简介结构

**第一段 — 建立日常，然后打破它**
- 目的：让读者先代入一个"正常"的世界或人物，然后用一句话把这种正常感打碎
- 写法：1-2句建立主角或案件背景的"表面正常"，第3句引入那个使一切不同的事件
- 字数：约100字

**第二段 — 呈现谜题的核心张力**
- 目的：告诉读者这个案件的独特之处，为什么这个谜题与众不同
- 写法：案件的诡异之处 + 主角面临的特定困境（不只是"调查凶手"，而是更具体的障碍）
- 字数：约150字

**第三段 — 埋下大阴谋的钩子**
- 目的：让读者感到"背后还有更大的事情"，同时结束在一个开放性的悬念上
- 写法：一个暗示性的问题或线索，指向更深的真相，但不说清楚是什么
- 字数：约80字

