### 命名哲学

悬疑推理书名的美学核心：**表面平静，底下有暗流**。最好的悬疑书名往往不是直接说"这是一个凶杀案"，而是用一个看似普通的词或短语制造认知张力——读完书后，再回看书名，才明白它的真正含义。

中文悬疑小说命名的特点：简洁、有意象、具有回溯性意义（结尾时才完全理解书名的重量）。

