### 书名优劣对比

| 好书名 | 差书名 | 原因分析 |
|--------|--------|---------|
| 有意象、有回溯性（结尾才懂全部含义） | 直白描述案件（"连环谋杀案"） | 直白书名没有神秘感，也没有回溯性意义 |
| 简洁有力（2-5字最佳） | 冗长解释性 | 书名不是简介，不需要解释 |
| 暗示而非直说 | 过度强调黑暗感（"死亡之……"） | "死亡"、"杀手"等词已经过度使用，没有新鲜感 |
| 有语言美感（音节、字形） | 网文流水线标题（"重生之……"格式） | 类型感过重，缺乏文学质感 |

