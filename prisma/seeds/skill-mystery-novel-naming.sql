### 常见书名格式

**格式一：物/地点名（表面平静型）**
- 结构：一个普通的名词，但放在悬疑语境下有不祥感
- 示例方向：以某个案件关键地点或关键物证为名（不直接说出其意义）
- 适合：氛围型悬疑，书名的含义在结尾才完全清晰
- 注意：名词本身要有一定的意象感，不能太普通（"椅子"不是好书名，"红门"可以是）

**格式二：数字/时间型**
- 结构：精确的数字或时间暗示某种不寻常的规律
- 示例方向：与案件规律相关的数字，或关键的时间节点
- 适合：有连环规律的案件，智识游戏氛围
- 注意：数字需要与故事有具体关联，而不只是看起来神秘

**格式三：反差对立型**
- 结构：两个语义对立的词，暗示表里不一
- 示例方向：美好与残酷的对比、日常与异常的对比
- 适合：人性拷问型悬疑，揭示表面文明下的黑暗
- 注意：对立要有张力，不能太直白（"善与恶"太直白）

**格式四：问句/悬问型**
- 结构：一个不立即给出答案的问题
- 示例方向：关于身份的问题、关于事件的问题
- 适合：以谜题本身作为最大卖点的推理小说
- 注意：问句书名容易过于直白，需要精心措辞

**格式五：人名/称谓型**
- 结构：以关键角色的名字或身份为题
- 示例方向：以受害者、幕后主使或一类受害者群体命名
- 适合：人物驱动型悬疑，读者在读完后才完全理解这个名字的重量
- 注意：人名作为书名需要这个名字本身有意象感或具有戏剧性

