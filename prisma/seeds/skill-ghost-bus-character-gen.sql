### 人形完整度的叙事写法

人形破绽是规则流最重要的视觉叙事元素，需要服从**由轻到重**的出场原则：

**完整（初看完全像人）**：
- 适合：故事开场时，绝大多数乘客应该处于这个层次
- 写法：不要描述任何异常，只是一个坐在那里的人。异常感来自于「某件事让我觉得我不应该和他说话」——这种模糊的直觉比具体描述更有效

**轻微破绽（偶尔的细节不对）**：
- 适合：故事中段，「背景乘客」开始出现破绽
- 写法举例：坐了很久却一直保持完全相同的姿势；肩膀的轮廓从某个角度看不像正常的锁骨；说话时嘴唇动作和声音有细微的延迟
- 关键：这些破绽应该是「主角觉得可能是自己眼花」的程度

**明显异常（需要刻意不去看才能维持正常感）**：
- 适合：中后段，某些乘客的伪装开始松动
- 写法举例：呼吸声不从胸口发出；眼白的比例太大，黑色的虹膜太小；手指的关节有一个向错误方向弯曲
- 关键：这些细节需要「主角刻意注意才发现」，而不是一眼就看出来

**几乎不像人（只保留最基础的人形轮廓）**：
- 适合：高潮前的压迫段落，或者揭示某个重要乘客真实身份的时刻
- 写法举例：在昏暗灯光下投下的影子形状不对；声音从不应该有声音的地方发出；动作时间不连续，像是每隔几秒才更新一帧
- 关键：此时「人形」仍然存在，但只剩了空壳

**完全非人（停止伪装）**：
- 适合：故事最紧张的时刻，或终点接近时
- 写法：可以直接描述，但不要试图用太多词汇定义它的形态——模糊的描述（「它已经不像任何有名字的东西」）比具体的描述更令人恐惧
- 关键：到达这一级别时，视觉细节已经不再是关键，**它的行动**才是恐惧来源

