### 乘客的行为模式设计

**重复性行为**（针对亡魂/执念型乘客）：
- 设计原则：行为应该**有具体内容**，不是「一直坐在那里」，而是「一直翻那个皮包，从来没停下来，翻到第几次了我没数，但她的表情从来没有变化」
- 行为应该暗示她的生前故事，但不需要解释

**等待性行为**（针对捕猎者/危险存在）：
- 设计原则：他们在等待主角犯错，所以行为是**被动观察型**
- 写法：「它只是坐在那里。这就是最令我不安的地方——它没有做任何事情，只是坐在那里，等着什么」

**程序性行为**（针对规则守护者）：
- 设计原则：每一个动作都是在执行某种不可见的规程
- 写法：「它走过来，低下头，在座位背面检查了什么，然后走开了。好像在执行某种例行检查。它到第五排停了一秒，然后继续往后走。我不知道第五排有什么。我再也没有回头看第五排。」

