### 乘客阵容的整体设计

**阵容建议（短篇，5-8个乘客位置）**：
- 1-2 个「当时看起来正常、后来发现不对」的背景乘客（轻微破绽级）
- 1 个边缘状态乘客（提供部分信息或人情感）
- 1-2 个明显的危险存在（明显异常级）
- 1 个与主角有直接互动的规则守护者或索命者
- 司机（独立于其他分类）

**乘客变化的节奏**：
- 每一站可以有新乘客上车，或者某个乘客「消失」（不是下车，而是不见了）
- 随着故事进行，背景乘客的人形完整度应该整体下降

