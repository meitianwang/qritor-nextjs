### 乘客类型的叙事功能

**普通人乘客**（和主角一样误上了车）：
- 叙事功能：提供盟友可能，或揭示「如果主角不努力，最终也会变成这样」
- 设计要点：他们和主角一样挣扎吗？还是已经放弃？还是他们对主角隐瞒了什么？
- 常见陷阱：只有一个功能（要么帮助主角要么害死主角），缺乏中间状态的复杂性

**边缘状态乘客**（还没完全死/还没完全活着）：
- 叙事功能：最有叙事空间的类型——他们可以提供信息，也可以是误导，存在本身就是对「人的定义」的挑战
- 设计要点：他们是否知道自己的状态？他们对主角是否有情感？他们的行为是否一致？
- 常见陷阱：变成「给主角传授规则的导师」，丧失了自己的故事

**规则守护者**（维持车上秩序的存在）：
- 叙事功能：他们是「系统」的代理人，与主角的冲突不是个人恩怨，而是「系统」在处理异常
- 设计要点：他们执行规则的方式是冷酷的程序性，而非仇恨——行政式的威胁比个人化的威胁更令人绝望
- 常见陷阱：让他们变成「反派」，破坏了规则流「规则才是真正恐惧来源」的核心

**索命者**（以乘客形态出现的危险实体）：
- 叙事功能：提供具体的、有面孔的危险，是当主角犯错时的「后果」
- 设计要点：他们的人形破绽要服从**渐进原则**——主角第一次看到时可能没有察觉问题，第二次才发现了
- 常见陷阱：开场就写得非常明显可怕，之后「更可怕的存在」失去对比基准

**司机**（特殊类别）：
- 叙事功能：规则的执行者，整个系统运转的维持者
- 设计要点：司机是规则的一部分，不是故事的主角。他不主动伤害，但也不帮助。他只是开车。
- 核心原则：司机的威胁感来自于「他存在于这里是正常的」——这种正常感本身就是深度异常

