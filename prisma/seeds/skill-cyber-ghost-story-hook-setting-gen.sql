### 初始理性化解释的设计

**主角第一次必须有一个可信的解释**，否则故事失去张力：

| 异常类型 | 最可信的理性化解释 |
|---------|----------------|
| 视觉残影 | 压缩伪影/红外感光/夜视模式误判 |
| 动作触发 | 动作侦测灵敏度过高/风吹窗帘 |
| 音箱自动播放 | 误触发语音指令/定时任务未关闭 |
| 机器人异常路径 | 固件更新改变了路径算法 |
| 灯光自动开关 | 定时设置/传感器故障 |

**理性化解释的有效期**：
解释必须**在短期内完全可信**，但需要设计一个可以被后续事件推翻的「漏洞」。漏洞不必在第一次出现，可以在第三次相同异常出现后，主角才意识到「如果是算法误判，为什么时间点这么精确？」

