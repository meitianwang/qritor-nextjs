### 三种钩子类型及叙事优势

#### 视觉证据型（监控/摄像头）
- 核心特征：有可回放、可截图的客观记录
- 叙事优势：「我有录像为证」是这个类型独有的；主角可以反复回放，每次回放都能发现新的细节
- 叙事挑战：「视频可以被修改」「算法误判」「压缩伪影」是三种主要的理性化解释，需要设计足够的细节来让这些解释最终站不住脚
- 最强钩子结构：录像里有你，你的背后有什么，但**你自己不知道**——主角事后看回放时发现的，比现场目击更令人不安（「那时候我背后有东西，而我完全不知道」）

#### 音频证据型（智能音箱/婴儿监视器）
- 核心特征：内容可以被逐字转述，无法被「视觉误判」解释
- 叙事优势：音频内容的具体性是恐惧的来源——不是「奇怪的声音」，而是「一段可以被字面理解的话」
- 叙事挑战：声音更难以「证明给别人听」（可能是幻听/其他来源的声音串入）
- 最强钩子结构：播放了主角能够辨认的声音（已故亲人的声音/主角没说过的话被重复/某段只有特定人才会知道的对话）

#### 行为证据型（扫地机器人/智能灯光/门锁）
- 核心特征：设备的行为模式有规律性异常，而非单次事件
- 叙事优势：规律性让理性化解释越来越难以维持（「一次可以是故障，但第七次绕开完全相同的区域就不是故障了」）
- 叙事挑战：需要积累多次观察，单次行为异常的钩子较弱
- 最强钩子结构：规律性异常 + 主角终于找到异常背后的「图案」（路径绘制出了一个形状/时间节点有规律/异常总是在某件事发生时触发）

