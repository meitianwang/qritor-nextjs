### 无法回头时刻的设计

「无法回头」不是单次的冲击，而是**理性解释的最终崩塌**：

**推荐结构**：
1. 第一次：主角看到异常，解释为故障
2. 第二次：异常重现，解释依然可信（「可能是同类型的故障」）
3. 第三次（或第三个细节）：新的细节让原来的解释在数学上不可能成立——不是「更令人害怕了」，而是「这件事已经**无法被解释为故障**了」

**「无法回头」的最佳触发条件**：
- 主角已经重置/修复了设备，但异常重现且与之前**完全相同**（精确相同排除了随机故障）
- 主角发现异常的出现时间与某件事有精确对应（时间相关性排除了巧合）
- 其他人也独立地见到了**同一件事**（独立目击排除了个人幻觉）

