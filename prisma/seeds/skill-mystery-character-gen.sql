### 字段生成指南

#### 姓名

悬疑推理小说的角色命名风格：
- **主角和核心人物**：需要有辨识度，但不要奇特（见主角设定章节）
- **嫌疑人群体**：命名要有多样性，避免读者混淆（最好一组嫌疑人的姓氏各不相同）
- **底层受害者**：往往需要名字普通，体现他们的社会隐形性
- **幕后主使**：如果需要隐藏身份，名字可以"太普通"或"太正常"以至于不引起注意

#### 角色定位

各定位的设计要点：

**主角**（见「主角设定」模块，此处不重复）

**搭档/助手**：
- 搭档不是主角的镜像，而是**互补**——主角的盲区正是搭档能弥补的地方
- 搭档应该有自己的视角和判断，有时会与主角不同意见（不能是纯粹的跟班）
- 搭档也应该有秘密——不是凶手，但可能知道某件不能说的事情
- 经典搭档类型：情感型vs逻辑型、守规矩型vs野路子型、社会型vs技术型

**嫌疑人（核心设计原则）**：
- **每个嫌疑人都需要两层动机**：
  - 第一层（表层）：他们撒谎、不配合或有不在场证明漏洞的**表面理由**
  - 第二层（深层）：他们真正在隐藏的东西（不一定是谋杀，可能是婚外情、财务问题、另一个小罪行）
- **嫌疑人的"可疑点"要分层**：有人看起来非常可疑但是无辜；有人看起来清白但实为凶手
- **每个嫌疑人应该与受害者有具体的关系**，而不只是"认识他"

**嫌疑人群像建议**（一个标准案件的嫌疑人配置）：
| 类型 | 作用 | 设计要点 |
|------|------|---------|
| **明显嫌疑人** | 分散读者注意力，后来被排除 | 动机明显但证据不成立 |
| **无辜的秘密持有者** | 因为隐藏无关秘密而看起来有罪 | 揭示后读者对他的同情 |
| **真凶** | 真正的凶手 | 表面最不可疑，动机最深层 |
| **受保护的人** | 有人在帮他/她隐瞒 | 揭示保护关系是中段转折点 |

**受害者**：
- 受害者不是空洞的符号——他/她死之前是个有欲望、有恐惧、有秘密的人
- 受害者的秘密往往就是他们被杀的原因
- 受害者的过去（5-10年前的某段经历）常常是解开谜题的关键
- 如果是多个受害者：他们之间的共同点是最重要的线索，要精心设计

**证人**：
- 证人最容易被写成信息提供机器，要避免这一点
- 每个重要证人都应该有一个理由不想提供完整信息
- 证人的可靠性（记忆是否准确？是否被施压？）本身就可以是谜题的一部分

**幕后主使**：
- 真正的幕后主使应该在全书中多次出现，但读者不认为他/她是嫌疑人
- 幕后主使的"不可疑性"来源于：看起来与案件无关、位置太高看起来不必要亲自动手、或者看起来是主角的盟友
- 回望全书，幕后主使的每一次出现都应该有可疑的细节——只是读者当时没往那里想

#### 核心动机

动机设计的层次：

**表层动机**（嫌疑人显露出来的）：
- 遗产/钱财
- 情感纠葛（嫉妒、背叛）
- 职业竞争
- 自我保护（掩盖过去的罪行）

**深层动机**（真正的推动力）：
- 往往比表层动机更具体、更个人
- 好的深层动机能引发读者的道德复杂感（"我理解他为什么这样做，但……"）
- 动机与角色性格要有内在一致性

**反动机设计**（用于干扰读者）：
- 有时给某个角色设计一个"太明显"的动机，让读者觉得他一定是凶手
- 后来揭示：那个"明显动机"导致了另一件事，但不是这个谋杀

#### 隐藏秘密

隐藏秘密的质量标准：
1. **与案件有关联**，但不一定是犯罪原因（可能是线索，可能是误导）
2. **解释了角色的可疑行为**——秘密揭露后，读者明白了之前看起来奇怪的行为
3. **不要所有人的秘密都是"有外遇"**——多样化秘密类型（财务问题、健康状况、过去的犯罪记录、与受害者的未公开关系等）

