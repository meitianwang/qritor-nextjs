### 角色阵容建议

**标准案件角色配置**（适用于中长篇的单个案件）：
1. **主角** - 侦探/法医/刑警
2. **搭档** - 性格互补，有独立视角
3. **嫌疑人A（明显嫌疑人）** - 动机明显，但最终无辜
4. **嫌疑人B（隐藏秘密型）** - 行为可疑，但秘密与案件无关
5. **嫌疑人C（真凶）** - 表面最正常，最后揭示
6. **受害者** - 有完整的过去和秘密
7. **证人** - 掌握关键信息但有理由不全说
8. **大阴谋关联人物** - 将本案与大阴谋连接的人物

