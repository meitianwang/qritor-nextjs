### 违规后果的层次设计

**全部违规都是即时死亡 = 故事失去张力**。当任何疏忽都是即死，读者感到主角完全被动，且故事无法持续。

**推荐的三级后果结构**：

```
【轻度违规——警告信号】
目的：让读者知道主角犯错了，但主角还有机会修正

典型后果：
- 某个存在转头注视主角几秒
- 车内温度骤降
- 灯光异常闪烁
- 手机屏幕出现无法解释的字

叙事功能：建立「规则是真实的」的读者认知，同时给主角（和读者）学习的机会

【中度违规——付出代价】
目的：违规有明确后果，但主角还可以继续

典型后果：
- 新的存在悄无声息地出现在座位上
- 身体出现无法解释的伤痕
- 失去一段短暂的记忆
- 规则增加了一条（违规触发了新规则）
- 某个曾经安全的区域变得不安全

叙事功能：提高赌注，让读者感到故事在推进而不是原地踏步

【重度违规——直接威胁生命】
目的：某些规则一旦违反，主角必须立即想办法解决

典型后果：
- 某个存在离开座位朝主角移动
- 下车权利被剥夺（到达下车站也无法下车）
- 终点站的到达时间被压缩
- 保护性道具失效

叙事功能：制造高潮节点，是故事中最紧张的段落
```

