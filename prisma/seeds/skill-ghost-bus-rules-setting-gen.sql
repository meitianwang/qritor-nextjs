### 规则漏洞的设计

**漏洞是主角利用的关键**。好的漏洞不是规则的破坏，而是规则边界的合法应用：

- **文字漏洞**：规则说「不能看后视镜」，但用手机摄像头的反光看——算「看」吗？
- **时间漏洞**：规则说「停站时不能站起来」，「停站」的定义是门开着还是车停下？
- **对象漏洞**：规则说「不能和乘客说话」，但司机算「乘客」吗？
- **主动漏洞**：规则说「不能拒绝让座」，主角主动起身是「让座」而非「被要求让座」

**漏洞的设计要点**：漏洞应该需要读者与主角共同思考才能发现。太明显的漏洞让故事显得规则体系设计不佳；漏洞应该在故事中被使用时，让读者有「对！就应该这样！」的感受。

