### 规则的内在逻辑类型

优秀的规则体系有统一的逻辑内核。同一个规则体系里的所有规则应该服从同一种底层逻辑：

#### A. 保护类规则（规则在保护乘客不被注意到）
- 底层逻辑：车上有某些东西，只要不触发它们的感知就安全
- 典型形式：「不要发出超过某音量的声音」「不要直视第三排」「停站时不能站起来」
- 逻辑反推：**什么东西**会被声音激活？**为什么**第三排特殊？停站时的危险**具体是什么**？

#### B. 平衡类规则（规则在维持某种契约或秩序）
- 底层逻辑：这辆车运行于某种协议之上，打破协议会触发后果
- 典型形式：「不能让别人知道你是活人」「必须在报站时说出站名」「不能拒绝让座」
- 逻辑反推：谁会注意到活人？报站是什么仪式？让座涉及什么权力关系？

#### C. 测试类规则（规则在筛选有资格通过的人）
- 底层逻辑：活着下车是一种资格，规则在测试主角是否值得
- 典型形式：「不能主动与其他乘客交流」「有人求助必须回应」「不能表现出恐惧」
- 逻辑反推：不交流是为了防止主角受影响？求助测试的是慈悲？恐惧意味着什么？

**混合使用**：可以组合两种类型，但同一条规则应该属于同一类型，不要让读者产生「这条规则到底是在保护我还是在测试我」的困惑。

