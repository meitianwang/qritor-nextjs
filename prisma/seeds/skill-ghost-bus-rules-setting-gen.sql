### 规则数量与结构

**建议 5-9 条规则**：
- 4 条以下：层次太少，危险节点不足
- 5-6 条：紧凑，每条都有充分展示空间，适合短篇
- 7-8 条：层次丰富，可设计优先级，适合中篇
- 9 条以上：密度高，需清晰分类（否则读者记不住）

**单条规则的写法**：

最清晰的规则形式是「禁止做 X」或「必须做 X」。避免过于复杂的条件句——复杂的条件逻辑应该在故事中自然揭示，而不是写在规则文本里。

