### 规则的发现弧

规则如何被主角知晓，是故事第一层叙事结构的核心：

**一次性全部告知**（一张字条/脑中植入/入车广播）
- 优点：读者与主角同步获得完整规则，可以从头观察危险
- 缺点：初始信息量大；之后的规则场景是「应用」而非「发现」
- 适用：短篇，或规则数量少（4-5条）

**逐步揭示**（每次面临危险才发现新规则）
- 优点：每条新规则的出现都是一个恐惧升级节点
- 缺点：需要设计合理的「为什么之前没有发现」，以及第一次违规时的「刚好没有死」
- 适用：中长篇

**主角自行摸索**（没有来源，只能观察和试错）
- 优点：最高张力，每个行为都可能是违规
- 缺点：需要主角有极强的观察力，且规则发现的逻辑必须严密
- 适用：氛围向短篇，强调生存本能

