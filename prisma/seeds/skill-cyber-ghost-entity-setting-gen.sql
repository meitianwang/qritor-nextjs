### 常见错误

1. **存在太主动**：从一开始就主动攻击主角，失去了科技灵异的「证据积累」叙事结构
2. **能力边界不清晰**：存在有时可以开门，有时不能，这种不一致性破坏了世界观的可信度
3. **存在没有特征性**：只是「一个鬼」，与设备的互动方式没有任何独特性，完全可以替换成任何灵异类型
4. **没有感知边界**：存在知道一切、能做一切，主角完全无能为力，故事失去张力
