### 存在的感知边界设计

感知边界是整个故事「主角能做什么」的边界：

**它知道主角的存在吗？**

| 知晓程度 | 恐惧质感 | 故事难度 |
|--------|---------|---------|
| **完全不知道** | 「它在我身边，但不知道我」——窥视与被窥视的错位 | 较低（主角只需要不「激活」它） |
| **知道有人在，不知道是谁** | 匿名威胁 | 中等 |
| **知道主角存在** | 被针对感 | 较高 |
| **对主角有具体的认知（知道他的名字/记忆/秘密）** | 最深层的个人侵犯感 | 最高 |

**它能做什么？**

三个能力层次：
```
【感知层】只能被感知，无法主动影响物理世界
→ 只有摄像头能看到它，它无法移动物体

【设备层】可以通过电信号影响设备，但无法影响物理世界
→ 可以播放音频、触发动作侦测，但无法开门

【物理层】可以对物理世界产生有限影响
→ 最高张力，但需要谨慎使用，不要让故事变成「一般的鬼片」
```

建议：大多数赛博灵异故事应该让存在停留在「设备层」——这保持了科技媒介的必要性，也保持了恐惧的特殊质感。一旦存在可以直接物理攻击，科技媒介就失去了叙事意义。

