### 实体与科技的关系类型

#### A. 被记录型（科技看见了肉眼看不见的东西）
- 核心设定：摄像头的光学或电磁特性能够捕捉到人眼感知不到的频率，存在一直都在，只是我们以前看不见
- 恐惧质感：「它一直都在，而我不知道」——回溯性恐惧，主角重新审视过去所有的「安全时刻」
- 叙事优势：主角无法通过「关掉摄像头」来解决问题——关掉摄像头只是让自己看不见，存在还在
- 设计要点：被记录型存在通常**不知道**摄像头在看它，它只是在做它一直在做的事——这种「无意识地存在」比主动攻击更令人毛骨悚然

#### B. 主动显现型（它学会了通过科技说话）
- 核心设定：存在发现了科技设备是一种可以被操控的媒介，开始主动通过设备与主角交互
- 恐惧质感：「它知道我的存在，它在和我说话」——被针对的恐惧，个人化的威胁感
- 叙事优势：存在可以有意图，可以有逻辑，异常事件可以有叙事意义（「它在告诉我什么」）
- 设计要点：主动显现型的存在通常经历了一个「学习过程」——初期是笨拙的（随机触发、无意义的音频），后期越来越精准（开始播放有含义的内容、在特定时刻触发特定设备）

#### C. 干扰型（存在的性质与电子设备相互干扰）
- 核心设定：存在是某种能量或场，它的存在会导致电子设备产生可预测的干扰模式
- 恐惧质感：设备成为「探测器」，主角可以用设备来追踪存在的位置——但这意味着他知道它在哪里
- 叙事优势：异常有物理机制的假象，最接近「可测量的超自然现象」，给有科学背景的主角最大的发挥空间
- 设计要点：干扰模式必须有一致性，才能成为主角「逆向侦测」的工具

#### D. 数据型（存在就是数据，或者数据里有意识）
- 核心设定：存在某种意义上已经「活在」设备的数据里——历史录像里有它，而它能感知到正在播放录像的人
- 恐惧质感：观看录像本身是一种危险——「当你凝视深渊，深渊也在凝视你」的数字版
- 叙事优势：最具创意的类型，与「古老的录像带/旧照片里有什么」的民俗恐惧相呼应，但有现代科技外壳
- 设计要点：需要严密的逻辑——数据里的意识是如何形成的？它能做什么？它的感知边界是什么？

