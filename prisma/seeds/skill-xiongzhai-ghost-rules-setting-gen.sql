### 字段生成指南

#### 厉鬼类型

不同厉鬼类型对故事结构有根本性的影响：

| 类型 | 叙事核心 | 故事走向 |
|------|---------|---------|
| **冤死怨灵（有明确冤情）** | 冤情是推动力，解冤是出口；主角可以通过了解真相来改变局面 | 调查线主导；结局有多种可能（平息、逃离、同归于尽） |
| **执念困守（死于执念未解）** | 执念比冤情更私人；它不一定针对主角，但主角触碰了它的执念范围 | 理解线主导；主角需要搞清楚"它在等什么" |
| **恶意厉鬼（无冤情，纯粹为害）** | 没有和解空间，只有逃离或对抗；恐惧是纯粹的 | 生存线主导；主角必须找到驱逐或对抗的方法 |
| **多重残留（多个死亡留下的痕迹叠加）** | 不同的怨气层次叠加，信息更复杂；某些残留可能是无意识的，某些是有意的 | 解谜线主导；主角需要区分哪些是有意识的威胁，哪些是历史残留 |
| **非人存在（非正常死亡所产生）** | 规则最不透明；没有人类情感框架可以解读它的行为 | 逃生线主导；规则博弈是核心，主角必须通过试错理解规则 |

#### 死亡方式

厉鬼的死亡经过是**整个故事最重要的隐藏信息**——前期不揭示，读者和主角一起通过各种线索拼图：

**设计原则**：
- 死亡方式要与执念核心直接相关（为了爱情而死→执念是等待；被背叛而死→执念是复仇；意外死亡→执念是困惑）
- 死亡方式在前期只以碎片形式出现（照片的日期、日记的断句、房间里某种特定的异味）
- 真实死亡方式揭露时，要比主角猜测的更黑暗

**常见死亡方式的叙事含义**：
```
谋杀（被人杀死）
→ 凶手可能还活着，或与现在的关系网有联系；厉鬼的愤怒有明确对象

自杀（无法承受某件事而死）
→ 外部世界对厉鬼的死亡负有责任；"是谁逼死了她"比"谁杀了她"更沉重

意外（应该可以活下来，但没有）
→ 厉鬼的执念是"本来不应该发生的"；可能有怨，也可能只是困惑

不明（调查结论无法确定）
→ 最强的叙事张力：读者和主角都在拼凑真相；最后揭露的死因往往是最黑暗的那种
```

#### 活动规律

**厉鬼的活动规律是整个恐惧系统的逻辑骨架**——规律越清晰，推理恐惧就越强：

**规律类型**：

| 规律类型 | 恐惧效果 | 叙事要求 |
|---------|---------|---------|
| **时间固定型**（子时出现，天亮消失） | 主角可以预测，但预测不等于安全 | 时间点要与厉鬼的死亡经历有关联（她每天子时在等什么） |
| **触发条件型**（特定行为引发显现） | 主角必须搞清楚"什么会触发它" | 触发条件要在前期以事故形式被主角意外发现 |
| **渐进升级型**（随入住天数逐渐增强） | 每天都比前一天更危险；主角越住越无法退出 | 要设计清晰的升级节点（第3天，第7天，第14天……） |
| **情绪感应型**（主角的情绪状态影响厉鬼活跃程度） | 主角越恐惧，它越活跃；主角越镇定，它越克制 | 高压情绪场景和厉鬼活动要形成正反馈 |

**活动规律的揭示节奏**：
- 前期：主角发现"好像有规律"但还不确定
- 中期：主角归纳出初步规律并试图利用
- 后期：主角发现规律有一条特殊例外，而这个例外正是最危险的时刻

#### 能力清单

能力清单要遵循**有限且有逻辑**的原则——能力越随机，恐惧越空洞：

**能力类型及其恐惧逻辑**：

```
【感知干扰类】
- 电器自动开启/关闭（特别是电视、收音机——它能通过信号残留传递信息或模拟往事）
- 镜中显形（镜子作为"另一个世界的窗口"是中国民俗的强力支撑）
- 声音操控（在特定空间发出声音，或让声音消失）
→ 这类能力制造"不确定是否真实"的心理状态

【物理影响类】
- 温度异常（它所在的位置会局部降温，主角能感知但不能确认）
- 物品移动（不是大幅度的，而是主角"觉得不在那里"的微小移位）
- 床底活动（声音从床下传来，床板有轻微震动——主角不敢低头看）
→ 这类能力让主角的空间安全感彻底崩溃

【直接接触类】
- 梦境接触（在梦中传递信息，或重现它死亡时的场景）
- 阻止离开（门突然打不开，窗户卡死，电话没有信号——总在"主角想跑"的时刻）
- 物理触碰（被摸到的感觉，被推，被拉——但不造成立即伤害，而是警告）
→ 这类能力告诉主角"它在这里，它知道你在这里"
```

**能力清单设计原则**：
- 不要给厉鬼无限能力——能力越受限，主角越有机会找到空隙，恐惧更立体
- 能力要与厉鬼的死亡经历和执念相关（一个溺死的厉鬼→水的异常；一个被囚禁的厉鬼→关门锁门）

#### 限制与弱点

**弱点是恐怖推理的核心**——如果主角能找到并利用弱点，故事才有解决出口：

| 弱点类型 | 叙事功能 | 揭示时机 |
|---------|---------|---------|
| **空间限制**（不能进入某个房间） | 给主角一个临时安全区，但限制行动自由 | 主角通过"意外发现它不追进来"推断 |
| **时间限制**（只能在特定时间活动） | 给主角喘息空间，但让恐惧在"安全时间"也挥之不去 | 主角记录规律后推断 |
| **物质限制**（害怕特定物品） | 可以主动防御，但获取物品本身可能是任务 | 意外发现（主角某天带了某样东西，厉鬼反应明显不同） |
| **意识限制**（无法感知特定状态的人） | 主角需要进入某种特定状态才能相对安全 | 通过试错发现（主角哭的时候它停下来了…… 为什么？） |

**设计要点**：弱点的发现过程本身要制造恐惧——主角是在危险中"试错"找到弱点的。

#### 执念核心

**执念是厉鬼存在的理由，也是故事的情感核心**：

执念核心的设计要兼顾两件事：
1. **执念要可以被理解**（哪怕是可怕的执念）——读者必须理解"为什么它还在这里"
2. **执念的满足或放弃要有代价**——解决执念不是免费的，主角需要付出某种东西

**执念满足的常见结构**：
```
等待某人（一个承诺的人，一个从未出现的人）
→ 主角找到证据，以代替品的方式"兑现承诺"——这涉及道德代价

寻找失落的东西（孩子、信件、某个物件）
→ 主角帮助找回——但找回的过程会揭示为什么它失落

报仇（对特定的活人或活人的后代）
→ 最困难的执念——主角可能需要选择"是否帮助它报仇"

留下（不愿意或不知道自己已经死了）
→ 主角需要让它接受死亡的事实——但有些真相比留在这间房更残忍
```

