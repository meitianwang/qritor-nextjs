### 常见错误

1. **厉鬼全能无限制**：能做任何事的厉鬼让主角完全被动，恐惧变成等死——必须有规则和限制才能产生推理空间
2. **规律设计后被自己打破**："子时出现"的规律在第五章就被打破，读者失去了恐惧框架
3. **执念太模糊**："它就是恨所有住这里的人"——这不是执念，这是随机危险，失去情感厚度
