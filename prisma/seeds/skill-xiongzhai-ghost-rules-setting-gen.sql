### 字段联动关系

- **厉鬼类型 → 空间.危险等级**：恶意厉鬼的活动区域危险等级普遍更高；执念困守的厉鬼主要集中在与执念相关的一个空间
- **活动规律 → 情节设计.关键事件**：每个活动规律的节点（子时、第7天等）都应对应情节中的关键恐惧事件
- **限制与弱点 → 故事结局**：主角如何利用弱点决定了故事能否有"自救"式结局
- **执念核心 → 凶宅档案.隐藏的核心秘密**：执念和秘密必须互相支撑，执念是秘密的情感版本

