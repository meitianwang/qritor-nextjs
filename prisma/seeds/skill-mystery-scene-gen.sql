### 字段生成指南

#### 地点名称

命名风格：
- **真实地名**：增加临场感（但要注意不过度具体化真实机构）
- **类型+地点**（如"废弃钢铁厂北厂区"）：明确地点性质，便于读者建立空间感
- **人物关联地名**（如"陈家老宅"）：地点与人物关联，增加情感重量
- **符号化地名**（如"红桥旅馆"）：名字本身带有暗示性，为后续揭示埋下意象

#### 地点类型

各地点类型的叙事功能：

| 地点类型 | 叙事功能 | 物证特征 | 氛围关键词 |
|---------|---------|---------|----------|
| **犯罪现场（第一案发地）** | 案件起点，核心证据场 | 物证最集中，痕迹最新鲜 | 震撼、压迫、细节密集 |
| **抛尸地点/二次现场** | 凶手行为轨迹分析 | 移动痕迹、转移时间窗口 | 阴暗、隐蔽、令人不安 |
| **嫌疑人据点/住所** | 揭示嫌疑人的内心世界 | 生活痕迹、隐藏物品、异常缺失 | 窥探、压抑、不安 |
| **机构场所** | 社会权力关系的空间化 | 文件、会议记录、访客记录 | 冷漠、官僚、表面正常 |
| **公共空间** | 目击证人的可能性最高 | 监控、人流、公共设施 | 匿名、对比（案件与日常的冲突） |
| **隐秘地点** | 秘密活动和最终对决 | 现场准备痕迹、长期使用证据 | 与外界隔绝感、危险 |
| **证据隐藏地** | 关键证据的容器 | 刻意隐藏的物品、伪装 | 平静下的潜藏、错落感 |

#### 地点描述

**有效的场景描写原则**：

1. **感官层次**：先给出视觉全景（这个地方是什么），然后进入气味（死亡/化学/食物/霉味）、声音（回声/静默/外部噪声的闯入），最后是触感（温度、材质、湿度）

2. **空间逻辑**：描述要服务于推理。需要说明：
   - 入口和出口在哪里（如何进来和出去）
   - 可见范围（从这个位置能看到什么）
   - 时间性（灰尘厚度说明多久没人来、食物腐烂程度说明时间）

3. **异常vs正常**：好的犯罪现场描写是在"一切正常"中发现"有什么不对"。不要全篇都在描写可疑之处，先建立正常感，再突出异常。

4. **空间与凶案逻辑的一致性**：如果犯罪发生在密室，那么密室的每一扇门和窗都需要有描述（为密室诡计服务）

**密室/不可能犯罪的场景设计**：
- 所有出口必须有状态描述（锁、门闩、监控）
- 空间的特殊结构（通风管、天窗、双层墙）要融入正常描写中，不要特意强调
- 给读者所有信息，但不提示哪个是关键

#### 关键线索

线索设计的"公平游戏"原则：
1. **线索必须在场景描写时提及**，不能在揭示阶段才突然出现
2. **关键线索不要标注**（不能写"主角注意到了一个重要的东西"），应该混在其他细节中
3. **线索的意义是多层次的**：同一个证据可能指向不同的解读，好的线索会引导错误解读，最终指向正确答案

**常见线索类型**：
- **物证**（凶器、血迹、指纹、纤维）：直接证据，但需要解读
- **时间线证据**（收据、通话记录、目击者）：建立时间框架
- **心理线索**（布置方式、删除的记录、不自然的整洁）：揭示凶手的心理特征
- **反证**（本来应该在的东西不在了）：缺席有时比存在更能说明问题

**陷阱线索（Misdirection）**：
- 故意设计一个看起来非常关键但实为误导的线索
- 误导线索必须有真实的解释（不是真凶放的，而是另一件无关事件留下的）
- 误导线索的解释应该在揭示阶段一并说明，让读者信服

