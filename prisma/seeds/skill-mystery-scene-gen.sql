### 场景阵容建议

**一个完整案件的场景配置**：
1. **主犯罪现场**（详细描写）- 案件起点，提供关键物证
2. **受害者生活空间**（中等描写）- 揭示受害者的过去和秘密
3. **调查据点**（轻描写）- 主角的工作空间，案件信息汇聚地
4. **1-2 个嫌疑人相关地点**（中等描写）- 分别对应不同嫌疑人
5. **最终对决地点**（详细描写）- 真相揭露的空间，通常有独特的象征意义

