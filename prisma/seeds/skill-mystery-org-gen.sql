### 阵容建议

**标准大阴谋的势力配置**：
1. **主要对立势力**（大阴谋的核心执行者，最终被揭露）
2. **保护性势力**（为主要势力提供体制保护的腐败机构）
3. **受害方阵营**（被阴谋压迫/利用的弱势群体，可能形成对抗力量）
4. **中间势力**（利益相关但立场未定，可以被主角争取）

