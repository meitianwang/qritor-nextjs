### 字段生成指南

#### 势力名称

命名策略：
- **官方机构类**：使用真实性强的机构名称格式（某某委员会、某某基金会、某某研究院）
- **商业集团类**：使用听起来正当的企业名称
- **犯罪组织类**：可以完全没有正式名称（内部有称呼，外界不知道其存在）
- **隐秘团体类**：历史感强的名称，暗示其存在时间的久远

**命名忌讳**：
- ❌ 太明显的恶意名称（"黑影帮"、"死神组织"）——真实的犯罪组织不会这样自称
- ❌ 太随意的缩写——除非有情节依据

#### 势力类型

各类型的核心叙事功能：

**犯罪集团**：
- 叙事功能：提供明显的敌对力量，是主角可以直接对抗的目标
- 设计要点：需要解释他们如何在执法体系下长期存在（保护伞、执法漏洞、渗透）
- 内部结构：层级清晰，底层实施者不知道顶层的全貌（信息隔离设计）

**腐败机构**：
- 叙事功能：制造"无处求助"的恐惧感，体制内的黑暗比体制外更令人绝望
- 设计要点：腐败不是全体的（否则不可信），而是关键节点的腐败（某个部门领导、某个审批环节）
- 核心张力：主角如何在体制内寻找可信任的盟友，同时规避潜在的内鬼

**秘密团体**：
- 叙事功能：历史感的神秘性，让大阴谋有更深的根源
- 设计要点：需要可信的存在逻辑（为什么要保密？保密了多久？谁知道它的存在？）
- 常见形式：宗教/哲学团体、精英阶层俱乐部、学术团体的变形

**商业集团**：
- 叙事功能：以利益为核心的冷血计算，最接近真实世界的现代恶
- 设计要点：揭示商业利益如何凌驾于人命之上，以及这种结构如何自我保护
- 法律漏洞：商业集团保护自己的方式往往是完全合法的（这使得主角的对抗更复杂）

**政治派系**：
- 叙事功能：权力腐化的叙事，揭示政治决策背后的肮脏交易
- 设计要点：不同派系之间的平衡关系，以及某个案件如何打破这种平衡
- 处理要点：中国背景下需要谨慎处理政治敏感度，通常以地方腐败而非中央政治为背景

**家族势力**：
- 叙事功能：世代积累的罪恶，揭示家族继承的不只是财富
- 设计要点：家族秘密的传承逻辑（谁知道？谁选择继续？谁想终止但无法终止？）
- 核心人物关系：家族内部的忠诚与背叛往往是解开谜题的关键

#### 对外形象

对外形象设计要求：**越光鲜越好**。真正有力的阴谋组织的外壳通常是：
- 获奖的慈善基金会
- 颇具声望的学术机构
- 有历史的老字号企业
- 积极参与社区建设的地方商会
- 专业媒体机构

外壳的设计细节：这个组织的公开活动是什么、它服务于谁的表面利益、普通公众对它的印象如何

#### 真实目的

真实目的的设计标准：
1. **利益足够大**，值得多条人命和长期掩盖
2. **动机可以理解**，甚至部分可以同情（最好的反派不是纯粹的恶，而是走错了路的人）
3. **与历史有关联**：真实目的往往根植于过去某个关键决策

**真实目的的三种结构**：
- **单纯利益型**：控制某种资源、市场或权力位置
- **历史延续型**：掩盖过去的某个罪行（当时认为合理，但现在会引发法律和道德追究）
- **理念驱动型**：相信自己在做正确的事，受害者是实现更大目标必要的代价

#### 内部弱点

内部弱点是主角最终能打破这个组织的切入点：

**可利用的内部弱点**：
- **忠诚度差异**：有成员因为良心不安而动摇
- **利益分配矛盾**：上层与底层之间的利益不均衡
- **知情范围的隔离**：底层成员不知道全貌，如果让他们知道，他们会选择离开
- **历史事件的参与者**：那些亲眼目睹初始罪行的人，有的还活着，有的留下了记录
- **外部压力的接缝**：当外部（媒体、调查机构）开始逼近，内部最薄弱的环节会先崩溃

