### 常见错误

1. **时代背景只是装饰**：时代背景改变了，故事不变——说明时代只是贴了一层皮，没有实质影响案件逻辑
2. **城市没有性格**：换一个城市名字故事完全一样，失去了地方感带来的真实质感
3. **调查障碍全靠蠢上司**：上司一直阻碍主角调查，但自己也没有合理的动机，只是扮演工具性障碍
