### 字段生成指南

#### 时代背景

不同时代的核心侦查条件差异：

| 时代 | 技术条件 | 社会特征 | 核心张力来源 |
|------|---------|---------|------------|
| **当代都市（2000+）** | DNA、监控、大数据、手机定位 | 信息过载，隐私消失，资本与权力深度勾连 | 技术反而成为掩护手段；数据可以被伪造或删除 |
| **1980-1990年代** | 无DNA，少量监控，纸质档案 | 改革开放带来财富与罪恶，社会价值观剧变 | 信息缺失、物证有限、体制尚不完善 |
| **改革开放早期（70-80年代初）** | 基本无技术手段，全靠走访 | 阶级固化、单位制社会、集体监控个人 | 人证为主，口供文化，证据标准模糊 |
| **民国时期** | 西式法医学引入初期 | 新旧秩序并存，帮派与政客，租界与华界 | 司法双轨、租界避风港、黑白两道复杂交织 |
| **架空近代** | 自由设定（可选择性保留某些限制） | 可强化某类社会矛盾 | 可定制核心张力 |

**当代悬疑的技术陷阱**：当代背景下，监控无处不在，手机信号可定位，DNA库可比对——作者必须为凶手的"反侦查"能力提供合理解释，否则"为什么没有监控拍到"会成为读者的持续质疑。

#### 城市设定

城市不只是地名，而是**犯罪生态的容器**。需要考虑：

**地理特征的叙事功能**：
- 港口/沿海城市：走私、人口流动、多国势力交汇
- 内陆省会：官场文化浓厚，地方保护主义，熟人社会
- 边境城市：双重身份、逃跑路线、法律灰色地带
- 特大城市：匿名性强（失踪容易），但监控密度高
- 小城镇：熟人社会（所有人认识，但谎言也更难戳破）

**城市的「性格」**：每座城市有自己的气质——南方某沿海城市的潮湿与商业气息、西北小城的干燥与封闭、历史古都的层叠与沉重。这种城市气质应渗透进案件的氛围描写。

**案件地图**：主要案件发生的几个核心地区，彼此之间的地理关系（地铁线、河流、工业区与住宅区的分布）。

#### 执法体系

执法体系设定是悬疑推理的核心机制之一——它决定了主角**能做什么和不能做什么**：

**体系类型的设定选项**：
- **正规刑侦队**：主角在体制内，有权力但受管控，升迁压力与破案压力并存
- **特设专案组**：临时成立的高级调查组，有更多自由度但政治风险更高
- **冷案中心**：专门处理未解案件，通常面临资源匮乏和机构漠视
- **独立执法单位**（如反腐部门、内部调查机构）：向谁汇报决定了谁是障碍
- **体制外**（私探/记者）：最大自由度，最小权力，靠人脉和信息交换生存

**腐败程度的设定**：
- **轻度腐败**：个别人员受到利益影响，体制基本正常运转
- **中度腐败**：特定部门或层级系统性受污染，主角需要甄别谁可以信任
- **重度腐败**：几乎不能在体制内寻求帮助，调查本身就是危险行为

#### 社会生态

社会生态为大阴谋提供土壤：

**权力结构分析**：
- 谁在这个城市/这个时代有真正的权力？是官员、商人、媒体人还是黑道？
- 他们之间的平衡关系是什么？什么会打破这个平衡？
- 普通人如何在这个权力结构中生存？他们的顺从或反抗如何影响案件？

**阶层与受害者群体**：
- 受害者来自哪个阶层？是底层弱势群体还是上层的意外卷入者？
- 不同阶层的受害者在执法体系中获得的关注度天差地别——这本身就可以是叙事张力

#### 调查障碍

调查障碍是制造叙事张力的关键机制。设计原则：

1. **障碍来自系统，而非蠢笨的个人**：聪明的障碍比笨蛋上司更有说服力
2. **障碍应该可以被智慧突破，而非暴力强冲**：障碍的突破过程本身就是推理过程
3. **障碍应分层**：眼前的小障碍（证据不足）→中层障碍（关键证人不配合）→核心障碍（整个体系在保护凶手）

**常见障碍类型**：
- 证据链不完整（物证损毁、现场被清理）
- 证人拒绝作证或改变证词（恐惧、收买）
- 上级压案（政治压力、维稳需求）
- 时效压力（案件即将被迫结案）
- 主角自身的权限限制（越级调查的风险）

