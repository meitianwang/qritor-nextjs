### 三、密闭空间的感官叙事

末班车全程在一辆车内，感官叙事是对抗「空间单调感」的主要工具：

#### 视觉的活用
- **车厢纵深**：前排-中部-后排是三个不同的「危险区域」，每一个区域的视觉细节都可以不同
- **窗的叙事功能**：窗内（车内的反光，能看到背后的一切）vs 窗外（不断变化的外部世界，是「当前距离人间有多远」的刻度）
- **光的变化**：荧光灯的闪烁、颜色偏移、某排灯亮度不同——光是视觉叙事的引导者

#### 听觉的活用（常被忽视）
- 车轮与轨道/路面的声音（是否正常？正常公交车有什么声音？这辆的声音哪里不对？）
- 乘客的声音或呼吸声（谁的呼吸声不在它该在的位置？）
- 报站广播（正常/扭曲/沉默——三种状态各有不同的恐惧质感）
- 主角自己的声音（打破沉默时声音的回响，是否感到自己的声音被吸收了）

#### 温度与气压的活用
- 「车厢比外面凉」是末班车的标准设定，但可以有更细的运用：某一排更凉、靠近某个存在时温度下降、规则被违反前温度骤降
- 气压感（头疼、耳鸣、「空气厚了一点」的感觉）

