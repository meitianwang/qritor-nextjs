### 常见写作错误

1. **每一段都有异常描写**：密度过高，读者对「不对劲」产生免疫，真正的高潮时刻失去冲击力
2. **规则相遇全部用相同节奏**：第一次遭遇和第十次遭遇使用相同的叙事结构，缺乏变化
3. **内心独白占比过大**：主角反复分析规则的内心戏超过实际行动，读者会分心
4. **感官描写只有视觉**：听觉、温度、气压的缺失让车厢空间变成了「看图说话」
5. **主角的情绪没有波动**：一直很怕，或者一直很冷静，都不真实。情绪的起伏是主角作为「人」的证明',
