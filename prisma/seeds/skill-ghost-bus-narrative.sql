### 一、规则相遇的写法

#### 第一次遭遇某条规则（建立规则的真实感）

**结构**：
1. 主角面临触发规则的情境（自然而然地，不要让主角「刻意」触发）
2. 主角的反应（遵守/不确定是否遵守/险些违规）
3. 周围存在的响应（轻微的改变——某个头转了，某个眼神，不需要立刻是威胁）
4. 主角的内化（意识到规则是真实的，对这条规则的理解）

**写法示例（节奏而非内容）**：
> 「[情境触发] → [主角内心的瞬间判断] → [身体反应先于思维] → [周围的变化，克制地描述] → [内心对规则的确认，不要说出来，用行为体现]」

**关键**：第一次规则相遇时，恐惧的来源是「这条规则真的有效」，而不是「我差点死掉」。轻微的后果最能建立规则的可信度。

#### 重复遭遇相同规则（规则的加深）

第二次、第三次面临同一条规则，写法应该变化：
- **第二次**：主角已经知道这条规则，但情境更复杂（要遵守这条规则，就得违反另一条）
- **第三次**：主角在更高压的情况下执行，执行过程有更多细节，或者这次险些执行失败

**避免**：用相同的文字和节奏描写对同一条规则的重复遭遇，读者会感到原地踏步。

#### 近乎违规（最高张力的规则场景）

主角几乎违规，在最后一刻遵守——这是规则流中最能调动读者神经的场景类型：

**写法技巧**：
- 放慢时间（用更短的句子，更细的感官描写）
- 让「险些违规的动作」在进行中被中断（手已经抬起来了，眼神已经往那个方向移了）
- 中断的力量来自于「主角自己记起规则」，而不是「外部干预」——自我控制的克制感比被外力阻止更令人窒息

