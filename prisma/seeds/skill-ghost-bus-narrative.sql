### 二、人形崩解的渐进描写

人形完整度的写法是规则流最独特的视觉叙事挑战。不同层次需要不同的描写策略：

#### 完整（初次看见时）

**写法原则**：完全不描述异常，只写「正常感」——但让正常感有一点微妙的「过于正常」的质感。

> 「他坐在那里，穿着一件不特别的蓝色外套，手放在膝盖上，看着窗外。我第一次看见他时没有感觉到任何不对劲，我甚至想了一下他是不是在哪里见过，但想不起来。」

**技巧**：用「主角说不清楚哪里不对」的方式暗示异常，比直接描写更有效。

#### 轻微破绽（第二次仔细打量时发现）

**写法原则**：具体的一个细节，其他一切正常。

```
好的破绽描写：
- 具体的（「他的拇指弯向了手背方向，不应该弯到那个角度，但他的表情没有丝毫痛意」）
- 可能是错觉的（「灯光可能是问题，但我不记得车里的灯什么时候会让人产生这种视觉错误」）
- 主角的反应是「也许我眼花了」（而不是「我顿时感到一阵恐惧」——过早反应破坏了悬疑）

不好的破绽描写：
- 太笼统（「他浑身散发着一股不对劲的气息」）
- 主角立刻确认了异常并产生强烈反应（太快，没有让读者参与判断的空间）
```

#### 明显异常（需要刻意不去看才能维持正常感）

**写法原则**：多个细节，但仍然处于「可以解释」的边缘。主角开始主动回避视线。

**叙事技巧**：用「主角不想看/不敢看」的回避行为来强调异常，比直接描写异常本身更有效。
> 「我不再往那个方向看，这是在第三站之后的事。不是因为我害怕——我告诉自己——而是因为如果我继续看，我会需要承认我看到的那些东西。」

#### 几乎不像人

**写法原则**：不再试图用「可以解释」来缓和，直接描写异常，但保持克制的语言。

**关键**：此时恐惧的来源已经从「不确定」变成了「确定」——语言可以更直接，但不要堆砌形容词，具体的细节比「可怕」「恐惧」「让人毛骨悚然」有效一百倍。

#### 完全非人（停止伪装）

**写法原则**：停止描述形态，转向描述「它的行动」和「主角的反应」。

> 不要：「它已经完全变形，成了一个说不清楚形状的存在，浑身散发着死亡气息。」
>
> 应该：「它移动了。我只注意到一件事：它过来的速度，和它坐着时完全不同。」

