### 四、主角精神状态的管理

**精神状态的变化弧**（不是线性崩溃，而是波浪形）：

```
开始：震惊（「这不可能是真的」）
→ 否认失败：勉强接受现实
→ 分析/解谜状态：主角开始用理性应对（这是相对平静的阶段）
→ 第一次危机：理性被冲破，直接的恐惧
→ 重建控制：主角找到新的支点（「只要我遵守规则就安全」）
→ 支点动摇：规则的可靠性开始受到质疑
→ 最深的恐惧：没有规则可以依赖的时刻
→ 突破与决断
```

**写作技巧**：主角不能一直处于「高恐惧状态」，需要安排「喘息段落」——这些段落让读者和主角一起稍微放松，然后下一次冲击才有对比效果。

**主角的行动性**：好的规则流主角不是被动地「经历恐惧」，而是在每一段时间里都有明确的「当前目标」（理解某条规则/找到某样东西/等到某一站）。行动性让主角成为真实的人，而不是恐惧体验的被动容器。

