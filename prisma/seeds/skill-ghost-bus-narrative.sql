### 五、节奏技巧

**句子长度与节奏**：
- 高张力时刻：短句。「车停了。灯灭了。只有我们还在。」
- 恐惧积累时刻：中等长度句子，有子句和延伸，制造阅读的延迟感
- 相对安静时刻：长句，舒缓，让读者跟着主角的呼吸放慢

**章节/段落结尾的钩子**：
- 问题钩子：「我当时不知道，那条我一直以为是最重要的规则，其实只是掩护。」
- 状态变化钩子：「然后，第六排的灯亮了。在我们上车之后，那排灯一直是灭的。」
- 倒计时钩子：「还有三站。」

