### 字段联动关系

- **核心秘密 → 势力.真实目的**：大阴谋的核心秘密就是各方势力不惜一切保护的东西
- **案件关联逻辑 → 角色.隐藏秘密**：每个受害者/嫌疑人的隐藏秘密应与案件关联逻辑对应
- **真相层次 → 故事钩子.读者预期**：层层揭秘的节奏需要在开篇就对读者作出承诺
- **主角命运关联 → 主角设定.致命弱点**：命运关联利用了主角的致命弱点，使得真相对他的伤害最大

