### 知晓程度与信息落差

不同人物知晓程度的差异是创造叙事张力的主要工具：

| 人物 | 建议知晓程度 | 叙事价值 |
|------|-----------|---------|
| 主角 | 逐步了解 | 主要视角 |
| 家庭成员 | 察觉但合理化 | 孤立机制 |
| 邻居 | 部分知晓但沉默 | 背景压迫感 |
| 物业人员 | 完全知晓（某些方面） | 信息隐瞒的压迫 |
| 技术支持 | 见过但选择解释为故障 | 理性防线的守护者 |

