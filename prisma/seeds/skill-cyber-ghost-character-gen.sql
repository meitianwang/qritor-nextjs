### 邻居：独立的证据来源

邻居是赛博灵异特有的高价值人物类型，因为他们有独立的设备系统和独立的生活空间：

**「独立目击者」型邻居**：
- 叙事功能：他们有自己的监控录像，可以从另一个角度确认（或否认）异常
- 设计要点：他们的设备记录到的和主角的设备记录到的应该**相关但不相同**——相同则太确定，无关则没有叙事价值
- 最强场景：「我去问了302的老陈，他说他的门铃摄像头也拍到了什么，但他把录像删了——他说他不想知道」

**「不愿提及」型邻居**：
- 叙事功能：暗示「不止主角一个人遇到了这些」，但整栋楼都在集体沉默
- 设计要点：邻居的沉默需要有动机（恐惧？过去的经历？集体协议？）

