### 主角的科技关系决定故事基调

**IT从业者/工程师型主角**：
- 优势：专业知识让他能给出最精确的技术解释，也让他的「最终无法解释」最有说服力
- 恐惧特质：「我穷尽了所有技术手段，这仍然无法解释」——知识成为自我折磨的工具
- 叙事特色：可以写「主角在设备日志里找到了异常的规律，越找越不对」的解谜过程
- 常见危险：变成工具性的「侦探型主角」，失去了情感脆弱性

**普通科技用户型主角**（最常见）：
- 优势：最高代入感，大多数读者的体验
- 恐惧特质：设备是「黑箱」，主角不懂也不想懂——直到它开始不正常
- 叙事特色：「我去网上查，说这是正常的，但我还是觉得不对」——依赖外部解释但内心无法信服
- 常见危险：主角显得被动，只能「等待异常」而不能主动调查

**对科技有反感/恐惧的主角**（对立型）：
- 优势：内在冲突最强——「我一直反对装这些设备，现在它们出了问题我反而最依赖它们来证明发生了什么」
- 恐惧特质：科技悖论——摄像头既是恐惧的来源，也是唯一能证明存在的东西
- 叙事特色：主角和设备之间的关系随着故事进展发生根本性的逆转

