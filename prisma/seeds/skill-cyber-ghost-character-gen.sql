### 家庭成员：不相信者的叙事功能

不相信主角的家庭成员是赛博灵异故事最重要的「孤立机制」：

**「合理的不相信」**（最有说服力）：
- 他们没有亲眼看到异常
- 他们给出的解释在技术上是合理的
- 他们的不相信出于对主角的关心（「你是不是最近太累了/压力太大了」）

**设计要点**：不相信的家庭成员不应该是「纸板人」——他们必须有自己的合理性。一个完全不相信的家庭成员比一个完全相信的家庭成员提供更强的叙事张力，但他们的不相信必须让读者也能理解「如果我是他/她，我可能也不会相信」。

**「被动目击者」**（增加复杂性）：
- 家庭成员隐约察觉到了什么，但选择不说
- 他们的回避行为本身成为谜题的一部分
- 最终揭示他们的「知晓程度」是故事中段的重要转折

