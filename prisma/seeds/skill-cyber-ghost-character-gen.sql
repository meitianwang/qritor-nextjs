### 技术支持人员：理性解释的专业代理

技术支持人员来检修设备时，是故事中「理性解释机制」的具象化：

**设计要点**：
- 他们总能找到一个「合理」的技术原因（否则叙事就太快倒向灵异）
- 他们的解释应该在当时是可信的，但事后被新的异常推翻
- 可以设计一个「见过太多奇怪故障」的技术支持人员，他在检修时表现出异常的沉默或快速离开——暗示他也看到了什么

**最强技术支持场景**：「修好了。他走的时候我问他是什么问题。他说是固件冲突。我说之前也出现过相同的问题，他停顿了一下，说那可能需要换一台设备。他说话时没有看我。」

