### 物业/安保人员：系统级别的接触者

物业人员有整栋楼的监控权限，这使他们成为「知道太多的人」：

**「什么都看到了」型物业人员**：
- 叙事功能：他见过太多「不该有的东西」，但他的工作要求他把这些当作「故障」处理
- 恐惧质感：「职业去敏感化」——他面对异常的冷漠比恐惧更令人不安
- 经典场景：「他翻出了主角要求的那段录像，看了两秒，说''这个需要技术部门来处理''，然后把屏幕转过去不让主角看」

**「知道但不说」型物业人员**：
- 叙事功能：他知道这个单元/这栋楼有「历史」，但处于某种原因选择不主动告知
- 叙事价值：制造「信息隐瞒」的压迫感

