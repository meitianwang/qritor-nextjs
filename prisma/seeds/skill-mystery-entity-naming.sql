## 一、角色命名

### 命名哲学

悬疑推理的角色命名核心是**真实感 + 辨识度**。名字应该让读者相信这是真实存在的人，同时足够不同以便区分。在中文悬疑语境下，命名要符合时代特征（当代人名、民国人名等有各自的规律）。

### 主角

主角命名的特点：
- **有质感但不奇特**：不用过于常见的名字（"王强"、"张伟"），也不用过于奇特的名字
- **适合在书中反复出现**：两字名或带姓三字名，音节要顺口
- **有辨识度**：在嫌疑人群中能被读者一眼识别
- 当代背景：可以参考1970-1990年代出生的真实人名风格
- 民国背景：单字名或带字的命名（如"某某民"、"某某轩"）较常见

### 嫌疑人群体

嫌疑人命名规范：
- **姓氏多样化**：一组嫌疑人（5-8人）的姓氏应尽量不重复
- **姓氏的阶层暗示**：某些姓氏在特定地域背景下有阶层含义（本地大家族姓氏 vs 外来移民常见姓氏）
- **名字不宜过于相似**：避免音节接近的名字（如"王林"和"王磊"在紧张阅读时容易混淆）
- **职业匹配感**：医生、商人、知识分子、基层工人的名字风格往往有差异

嫌疑人命名的阶层参考：
| 阶层/职业 | 命名风格 |
|----------|---------|
| 精英/知识阶层 | 常见有文化意象的字（如：明、哲、宇、文）|
| 商界人士 | 偏向实际、吉祥（如：财、达、盛）|
| 普通市民 | 更日常化的字，有时带时代烙印（如：建国、爱华）|
| 底层/边缘人物 | 随意、缺乏设计感的名字（体现其社会能见度低）|

### 受害者

受害者命名的特殊考量：
- 受害者的名字应该让读者记住，因为整本书都在围绕这个人展开
- 避免让受害者名字与嫌疑人名字混淆
- 如果受害者是底层弱势群体（工人、流浪者），名字可以平凡，但要有独特之处让读者记住
- 如果受害者与阴谋深度相关，名字可以有意象性含义（但不要太明显）

### 幕后主使

幕后主使的命名策略：
- 名字本身不应该引起读者的怀疑（不能叫"某某阴"或任何暗示黑暗的字）
- 可以选择一个"太过普通"或"令人放松警惕"的名字
- 揭晓身份后，读者回看这个名字，也许会发现其中有一个之前没注意到的双关意味

