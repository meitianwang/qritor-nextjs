## 二、案发地点命名

### 命名哲学

案发地点的名字承担双重功能：**方便读者空间定位**，以及**在某些情况下作为伏笔**（结尾时地点名字的真实含义被揭示）。

### 普通地点类（犯罪现场、调查地点）

- **实用性为主**：地点名字应让读者立即理解这是什么地方
- **地理真实感**：符合时代和城市特征（某某工业区、某某老弄堂、某某路XX号）
- **适度具体**：不要用过于模糊的"某处地下室"，要有具名性

示例方向：
- 机构：以功能命名（第三法医鉴定中心、某市刑侦支队）
- 商业场所：以行业+地名（南滨路XX餐厅、中关村某科技楼）
- 居民区：以年代或开发商命名，体现时代感（某某新村、某某花园小区）
- 废弃地点：以原用途命名（原XX钢铁厂仓库、废弃棉纺厂）

### 象征性地点（对故事有深层意义的地点）

这类地点的名字可以有意象性含义，但不要太明显：
- 名字本身普通，但与案件主题有隐秘呼应
- 读者在结尾时才理解这个名字的另一层含义
- 忌讳：名字太过"有意思"，读者第一次看到就感到"这个地方肯定很重要"

### 忌讳

- ❌ 所有案发地点命名风格雷同（全部是"某某大厦"或全部是代号）
- ❌ 地点名字过于强调黑暗感（"幽冥旅馆"——现实感崩塌）
- ❌ 地点名字在全书中前后不一致（换了说法让读者困惑）

