### 忌讳

- ❌ 同一嫌疑人群体中有两个同姓的角色（容易混淆）
- ❌ 音节高度相近的名字（"林浩"和"刘豪"）
- ❌ 给幕后主使一个"看起来很坏"的名字
- ❌ 过于刻意的"象征性名字"（名字直接暗示其角色功能）

