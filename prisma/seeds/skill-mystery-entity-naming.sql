## 三、势力命名

### 命名哲学

势力名称的核心是**合法外壳的真实感**。一个犯罪组织的合法名称应该听起来真实可信——就像真正存在于这个城市里。读者初次看到这个名字，不应该感到任何警觉。

### 合法外壳类势力

- **慈善/基金会**：某某公益基金会、某某文化发展基金会（听起来有公益感）
- **商业机构**：某某投资管理公司、某某实业集团
- **学术/研究机构**：某某社会研究院、某某历史文化研究所
- **行业协会**：某某同业公会、某某商业协会

命名要点：名称要有年代感（如果是老牌组织，名字里可以带有创立年代的时代感）

### 犯罪/秘密类势力

- 内部称呼可以是简短的代号或自然涌现的别称，而非正式名称
- 有些组织根本没有名字（这本身就可以是信息）
- 如果需要命名，应避免任何听起来像"邪恶组织"的名称

### 忌讳

- ❌ 名字太像虚构的反派组织（"黑羽帮"、"夜枭组织"）
- ❌ 合法外壳的名字太普通（缺乏真实感）
- ❌ 组织的内外两个名字搞混（书中需要始终清晰标识说的是哪个）

