### 常见错误

1. **主角太完美**：没有弱点的侦探让读者无法担忧，也无法共情，更无法感受到每次破案的代价
2. **弱点不影响破案**：弱点只存在于情感线，从不干扰案件推理，等于没有弱点
3. **职业与行为不符**：一个"法医"却在主动审讯嫌疑人、跑现场追凶，需要有情节理由支撑这种越界行为
4. **推理方法论太随意**：主角的推理过程缺乏一致的方法论，时而逻辑严密，时而完全凭感觉，读者感受不到智识享受
5. **背景故事太沉重**：主角有三段家庭悲剧、两段感情创伤、一个未解旧案……弱点堆砌反而让主角失去了真实感',
