### 字段生成指南

#### 姓名

悬疑推理小说的主角姓名有独特的美学要求：
- **中国当代背景**：平实的中文姓名为主，但可以有一个显得略微"不寻常"的字（体现人物的与众不同）
- **民国/历史背景**：字、号体系，或西化名字
- **架空背景**：可适度创意，但不要过于奇异（推理主角不同于玄幻主角，名字应有质感而非奇特感）
- 避免使用过于常见的"王强/李明"或明显的外国名音译（除非有情节设计原因）

#### 职业

六类职业的侦查视角差异：

| 职业 | 核心视角 | 天然优势 | 天然限制 |
|------|---------|---------|---------|
| **刑警/警探** | 程序与现场 | 调查权力、信息获取、强制手段 | 体制束缚、政治压力、过度依赖程序 |
| **法医** | 死亡与物证 | 解读遗体、理解死亡机制、无声的证据 | 行动受限、无法主动调查嫌疑人 |
| **心理画像师** | 动机与人性 | 理解罪犯心理、预判行为、识破谎言 | 缺乏实物证据能力、主观性风险 |
| **私家侦探** | 观察与游离 | 不受体制约束、跨案追踪 | 无法律权力、资源有限、身份边缘 |
| **检察官** | 证据与司法 | 法律授权、庭审视角、制度内的改变 | 程序束缚、不能直接调查 |
| **记者/调查记者** | 信息与舆论 | 采访权、揭露权、公众影响力 | 无执法权、保护信源的道德困境 |

**最有潜力的职业组合**：法医+前刑警、心理画像师+现役刑警、记者+业余侦探（有人脉无权力）

#### 职业背景

职业背景不只是简历，而是**理解主角为何在这里、为何是他**的关键。需要包含：
1. **从业经历**：多少年、在哪些地方工作过、处理过什么类型的案件
2. **关键转折点**：是什么让他成为今天这个人（一个未解的旧案、一次失败的调查、一个无辜者的错误定罪）
3. **社会关系网络**：他认识哪些有用的人（联络人、线人、昔日搭档、权贵友人）

#### 侦查风格

五种风格的文字表现形式不同：

| 风格 | 文字呈现重点 |
|------|------------|
| **逻辑推演型** | 大量内心推理过程、证据链构建、排除法 |
| **直觉洞察型** | 细节观察、肢体语言解读、"有什么不对劲"的感知 |
| **科学分析型** | 专业术语、技术程序、物证解读（法医报告、血迹形态分析） |
| **心理博弈型** | 人物心理分析、对话中的试探与反试探、构建嫌疑人动机 |
| **多线并行型** | 快节奏切换、多个调查线同时推进、复杂信息管理 |

#### 核心技能

核心技能应当**专业且具体**，而非笼统：
- ❌ "善于观察"（太笼统）
- ✓ "能通过微表情和肢体语言识别谎言，尤其擅长在审讯中制造认知压力"
- ❌ "法医知识丰富"
- ✓ "专精血迹形态分析，能从溅射模式还原犯罪现场的运动轨迹"

核心技能最好是主角**比任何人都好**的那一两样东西，而且这个技能要在多个案件中发挥决定性作用。

#### 致命弱点

这是设定中最重要的字段。优秀的推理主角的弱点分两类：

**A 类：影响侦查的职业性弱点**
- 容易对嫌疑人产生情感投射（以为自己理解他们，实则被操控）
- 对权威的执念（需要通过体制证明自己，或对体制有偏执性不信任）
- 对受害者过度代入（情绪化影响判断）
- 对自己方法论的过度自信（先入为主，排除异见）

**B 类：来自创伤的个人弱点**
- 一个未解的旧案（也许是家人，也许是亲眼目睹的无辜者被错判）
- 对某类罪犯的道德盲区（无法客观对待）
- 人际关系的系统性失败（没有人愿意在他旁边待太久）

**关键原则**：弱点和核心技能应来自同一个心理根源。
例：因童年目睹无辜者被错判而成为法庭律师，拥有无与伦比的证据解读能力，但同样因为这段经历，面对可能的司法不公会产生情绪化反应，有时会走程序之外的捷径。

#### 推理方法论

这是主角区别于"普通刑警"的核心。需要设计一套**独特且可辨识的思维框架**：

| 方法论类型 | 描述 |
|----------|------|
| **演绎消去法** | 排除不可能，剩下的就是答案（柯南道尔式） |
| **犯罪心理重建** | 先理解"为什么"，再推导"是谁" |
| **场景物理重建** | 从物证和现场痕迹还原犯罪过程（倒推时间线） |
| **人际动机网络** | 绘制所有人与受害者的关系图，找动机交集 |
| **模式识别** | 在看似不同的案件中发现重复的行为签名 |

