- 设备记录（「那段录像里的人形」「日志里那条记录后面的东西」）
- 关联历史（「3206 号房间之前住的那个」「三年前那件事之后就在这里的」）

**为什么不命名**：给非人存在命名意味着主角在「认识」它，赋予其身份感会减弱不确定性带来的恐惧。保持描述性称呼让它始终是「尚未被理解的存在」。

