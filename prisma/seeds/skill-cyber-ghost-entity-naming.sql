- App 中的设备昵称（通常是用户自己设置的）：「家里-大门」「我的音箱」
- 设备型号（技术性叙事时使用）：可以使用真实存在的设备型号（如「海康威视 DS-XXXXX」「小米 Xiaomi Speaker Pro」）

**公共设施（小区级）**：
- 编号系统：「C 栋 B1-C07 摄像头」「3 楼电梯间 E-03」
- 物业内部称呼：「3 号楼进门那台」「负一层停车场的那组」

**「有问题的那台设备」的叙事命名**：
当一台设备成为故事的核心恐惧来源后，叙事中对它的称呼应该逐渐从「型号/位置描述」变成「那台」「它」——这种转变标志着主角的心理变化，从「技术设备」到「有自主意识的存在」。

