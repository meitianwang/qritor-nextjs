### 一、人物命名

#### 主角

**命名策略**：
- 第一人称叙事：直接用「我」，不需要姓名
- 第三人称叙事：极其普通的名字（「林某」「王杰」「小陈」）；如果需要强调其技术背景，可以带出职业（「做 IT 的林某」）
- 主角的职业/技术背景对命名策略有影响：
  - 技术型主角（程序员/工程师）：命名可以更普通，甚至网名/用户名都可以
  - 非技术型主角（普通居民）：姓名普通即可，重要的是展示其对智能家居的初级认知

**避免**：
- 姓名暗示命运（「福来」「安生」这类名字会建立错误的阅读期待）
- 过于文艺的名字（「叶凉」「沈暮」）破坏了都市现代感

#### 邻居/物业角色

**命名策略**：
- 用楼层/房间号 + 普通称谓（「隔壁 3205 的老张」「楼上的那对夫妻」「三楼的小姑娘」）
- 物业人员：职位 + 姓氏（「李主任」「保安小吴」「客服小王」）
- 技术人员（客服/维修工）：职业标签 + 工号或姓氏（「客服 ID：KF-2891」「来修摄像头的师傅」）

**命名的叙事功能**：
以楼层/房间号为主的命名方式强化了智慧社区的「格子化」感——每个人首先是一个地址，然后才是一个人。这种命名方式与赛博灵异的「被数据化的生活」主题完全契合。

