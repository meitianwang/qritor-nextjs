### 四、异常实体命名

**核心建议：不要给异常实体起名**

赛博灵异的恐惧来源于「无法确认」——命名意味着理解，理解会消解恐惧。

**可以使用的指代方式**：

设备记录型指代：
- 「那段 3:17 的录像里的东西」
- 「扫地机器人禁区里的那个」
- 「音箱在 3:22 响应的那个请求的来源」

历史关联型指代：
- 「在这套房子发生那件事之前就在的东西」
- 「只在数据里存在的那个存在」
- 「摄像头比我更早看到的那个」

**例外**：
如果异常实体在故事中有真实身份（如某个已死亡的前住户），可以在故事后期通过历史调查揭示其真实名字——但这个名字应该同样非常普通（「原来是 3206 房间三年前去世的陈某」），普通的名字反而增加了「这是真实发生过的事」的质感。

**禁止使用的命名类型**：
- 神话化命名（「孤魂」「阴灵」「恶鬼」）
- 超自然概念化命名（「召唤者」「守护者」「入侵者」）
- 系统化编号（「目标 001」「实体 A」）——这会让故事向科幻方向倾斜

