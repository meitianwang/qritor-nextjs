### 二、区域命名

#### 公寓/室内空间

**命名原则**：直接使用现实中的房间名称，不做任何艺术化处理。

- 卧室/主卧/次卧
- 客厅/起居室
- 厨房/卫生间/阳台
- 储物间/杂物间/设备间

**强化定位感的方式**：
不用特殊名称，用设备分布来定位空间：「安了摄像头的那面墙」「音箱放在窗台的那个角」「扫地机器人充电座对面的角落」。

这种命名方式让空间的危险感与设备分布直接绑定。

#### 楼栋/小区公共空间

**命名原则**：用地址/编号命名，强化「这是真实存在的地方」的质感。

- 楼层 + 空间类型：「3 楼电梯前厅」「地下负一层停车场 C 区」「32 楼楼道」
- 小区编号命名：「C 栋 3206」「智慧家园 A 区 2 号楼」「11 号楼地下储藏室 B-07」
- 功能性命名：「大堂门禁闸机」「物业监控室」「电力设备间」

**避免**：
- 起艺术化的名字（「幽暗走廊」「恐怖电梯」）
- 用这些空间「发生了什么」来命名（不要叫「凶案现场」「死亡楼层」）

