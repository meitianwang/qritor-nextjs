### 道具类型与叙事功能

**护身符（提供某种保护）**：
- 叙事功能：降低主角在某类危险前的脆弱性，但不能消除危险
- 设计要点：保护的范围必须有限（「能让一个存在在接近到半步距离时停下来」比「能保护你不被任何东西伤害」更有张力）
- 代价方向：使用次数、使用条件（只在车停时有效）、使用成本（每次使用消耗主角一段记忆）

**凭证（证明某种身份或权利）**：
- 叙事功能：允许主角进行普通乘客不能进行的某些行动
- 典型形式：一张「特殊票根」、一个「标记」、某种只有某类乘客才有的物品
- 设计要点：凭证的有效性应该是可被质疑的（「如果有东西发现这张票根是伪造的/借来的/不完整的呢」）

**通行钥匙（打开特定区域或权限）**：
- 叙事功能：允许主角访问车厢内受限制的区域，或触发某种机制
- 典型形式：一把没有标记的钥匙、一个能让某扇门开启的代码、一个手势
- 设计要点：门另一侧是什么，在使用前应该是未知的。「打开了一扇门但不知道里面有什么」的状态是高张力时刻

**信息载体（记录了前人的经历或规则）**：
- 叙事功能：向主角传递关键信息，通常是规则的补充或修正
- 典型形式：一个皱巴巴的笔记本、手机里的旧录音、座位背后刻下的文字
- 设计要点：信息必须是**部分可读/部分损毁**的。完整的信息让主角太有优势；完全无法阅读的信息浪费了道具的功能。部分可读制造了「我知道一些，但还缺一块」的张力。

**感知工具（帮助主角察觉危险）**：
- 叙事功能：扩展主角的感知边界，发现肉眼无法直接看到的危险
- 典型形式：一面小镜子（反光能看到背后）、一个温度计（温度骤降=危险临近）、耳机（某些声音只有通过它才能听见）
- 设计要点：感知工具不能「直接揭示危险」，只能给出信号（「镜子里的反光不对」而不是「镜子里能看到那个东西的真实形态」）

**代价物品（需要为其功能付出某种代价）**：
- 叙事功能：制造高风险高回报的选择时刻
- 典型形式：「用它可以让所有乘客忽略你三十秒，但它每被使用一次，你就会忘记某个重要的人的脸」
- 设计要点：代价必须是主角真正不愿意付出的——无关紧要的代价等于没有代价

