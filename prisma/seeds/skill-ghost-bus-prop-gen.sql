### 道具的状态管理

「当前状态」字段（isDynamic）是道具叙事价值的动态载体：

- 道具的使用次数在减少
- 道具在损耗（从完好到裂缝到碎裂）
- 道具被临时移交给另一个角色
- 道具的功能在变化（变强或变弱）

**状态变化的叙事功能**：每当道具状态降级，读者都会感到「主角的资源在缩水」，这是末班车类故事特有的倒计时压力。

