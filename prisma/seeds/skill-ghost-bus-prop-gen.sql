### 道具设计的核心原则

**原则一：外观越普通越有力**

末班车规则流的美学是「日常物件的恐怖意义」——一张旧车票比一把魔法剑更符合这个类型的气质。

最有效的道具通常是：
- 日常随身物品（手机、钱包、钥匙）
- 他人遗留的物品（旧票根、皱巴巴的字条、不属于主角的手机）
- 普通但有仪式感的物品（一枚硬币、一个信封、一块手帕）

**原则二：功能必须有边界**

模糊的道具功能（「这个护身符能保护你」）比具体的道具功能（「这个护身符让你被盯上的那三秒内可以移动，但每次使用后它会变黑一点，变黑三次就失效了」）叙事价值低得多。具体的限制才能制造紧张感。

**原则三：所有能力都有代价**

没有代价的道具是对叙事张力的浪费。代价可以是：
- 使用次数限制（最简单也最有效）
- 使用后的副作用（身体症状、某种感知能力的减退）
- 需要满足特定条件才能激活（只在某些地方有效/只对某类存在有效）
- 道具在减弱或消耗中（每次使用都在接近失效）

