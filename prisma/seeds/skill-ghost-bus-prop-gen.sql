### 道具与规则体系的联动

道具应该与规则体系有内在联系，而不是独立于规则之外：

**道具帮助遵守规则**（让某条难以遵守的规则变得可能）：
- 例：有一条规则要求「不能发出声音」，而主角有一条会在特定时刻响铃的旧手机——道具就是「把手机静音」或「某个能隔绝声音的物品」

**道具帮助理解规则**：
- 例：某个感知工具让主角第一次真正看到了「不遵守规则会发生什么」，从而理解了某条规则的真正含义

**道具本身违反规则**（高风险选项）：
- 例：道具的功能非常有用，但使用它的方式触犯了车上的某条规则——主角必须在「利用道具」和「遵守规则」之间选择

