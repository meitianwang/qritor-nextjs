### 常见错误

1. **道具太强大**：一个功能没有限制的道具消解了故事的紧张感，主角可以用它解决所有问题
2. **道具没有与规则联动**：道具是独立于规则体系之外的工具，与整个故事的核心系统脱节
3. **道具的外观太「魔幻」**：在末班车的都市现代背景下，一把「神圣圣剑」完全不符合类型美学
4. **代价不够真实**：「每次使用会有一点点头痛」不是代价，不会让读者担心主角是否该使用它
5. **道具在故事结尾无缘无故地没有出现**：如果道具在前期被设定为关键物品，结尾时必须有交代（用掉了/失去了/用于某个关键时刻）',
